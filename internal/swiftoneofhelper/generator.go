// Package swift provides helper functions for Swift protobuf messages with oneof fields.
package swiftoneofhelper

import (
	"fmt"
	"strings"

	"google.golang.org/protobuf/compiler/protogen"
	"google.golang.org/protobuf/reflect/protoreflect"
)

const (
	swiftString = "String"
	swiftInt32  = "Int32"
	swiftInt64  = "Int64"
	swiftUInt32 = "UInt32"
	swiftUInt64 = "UInt64"
	swiftBool   = "Bool"
	swiftFloat  = "Float"
	swiftDouble = "Double"
	swiftData   = "Data"
	swiftAny    = "Any"
)

// GenerateHelpers generates Swift helper extensions for all messages with oneofs in the given file.
func GenerateHelpers(plugin *protogen.Plugin, file *protogen.File) {
	var ShouldGenerate func(messages []*protogen.Message) bool
	ShouldGenerate = func(messages []*protogen.Message) bool {
		for _, message := range messages {
			hasFields := len(message.Fields) > len(message.Oneofs)
			hasOneofs := len(message.Oneofs) > 0
			if hasFields || hasOneofs {
				return true
			}
			if ShouldGenerate(message.Messages) {
				return true
			}
		}
		return false
	}
	WriteHeader := func(g *protogen.GeneratedFile, file *protogen.File) {
		g.P("// Code generated by protoc-gen-swift-oneof-helper. DO NOT EDIT.")
		g.P("//")
		g.P("// Convenience initializers generated to easily and safely create messages with oneof types")
		g.P("//")
		g.P()
	}
	WriteImports := func(g *protogen.GeneratedFile, file *protogen.File) {
		g.P("import Foundation")
		g.P("import SwiftProtobuf")
		g.P()
	}

	if !ShouldGenerate(file.Messages) {
		return
	}

	filename := file.GeneratedFilenamePrefix + "_oneof_helpers.swift"
	g := plugin.NewGeneratedFile(filename, file.GoImportPath)

	WriteHeader(g, file)
	WriteImports(g, file)

	for _, message := range file.Messages {
		GenerateMessageHelpers(g, message)
	}
}

func GenerateMessageHelpers(g *protogen.GeneratedFile, message *protogen.Message) {
	GenerateInitializer := func(g *protogen.GeneratedFile, message *protogen.Message, name string) {
		var params []string
		var assignments []string

		// Add regular fields (non-oneof fields)
		for _, field := range message.Fields {
			if field.Oneof == nil {
				paramName := SwiftParamName(field.GoName)
				paramType := SwiftFieldType(field)
				params = append(params, fmt.Sprintf("%s: %s", paramName, paramType))
				assignments = append(assignments, fmt.Sprintf("\t\tself.%s = %s", paramName, paramName))
			}
		}

		// Add oneof fields
		for _, oneof := range message.Oneofs {
			paramName := SwiftParamName(oneof.GoName)
			paramType := fmt.Sprintf("OneOf_%s", oneof.GoName)
			params = append(params, fmt.Sprintf("%s: %s", paramName, paramType))
			assignments = append(assignments, fmt.Sprintf("\t\tself.%s = %s", paramName, paramName))
		}

		if len(params) > 0 {
			g.P("extension ", name, " {")
			g.P()
			g.P("\tinit(", strings.Join(params, ", "), ") {")
			g.P("\t\tself.init()")

			for _, assignment := range assignments {
				g.P(assignment)
			}

			g.P("\t}")
			g.P("}")
			g.P()
		}
	}

	// Generate main initializer if message has fields (regular fields + oneofs)
	hasRegularFields := len(message.Fields) > len(message.Oneofs)
	hasOneofs := len(message.Oneofs) > 0
	if hasRegularFields || hasOneofs {
		GenerateInitializer(g, message, message.GoIdent.GoName)
	}

	// Generate convenience initializers for nested message types
	for _, nestedMessage := range message.Messages {
		// Recursively process nested messages
		GenerateMessageHelpers(g, nestedMessage)
	}
}

// Converts protobuf field types to Swift types
func SwiftFieldType(field *protogen.Field) string {
	// Handle repeated fields (arrays)
	if field.Desc.IsList() {
		return "[" + SingleSwiftFieldType(field) + "]"
	}
	// Handle map fields (dictionaries)
	if field.Desc.IsMap() {
		return SwiftMapType(field)
	}
	return SingleSwiftFieldType(field)
}

// Returns the Swift dictionary type for map fields
func SwiftMapType(field *protogen.Field) string {
	keyType := SwiftMapKeyType(field.Desc.MapKey())
	valueType := SwiftMapValueType(field)
	return fmt.Sprintf("[%s: %s]", keyType, valueType)
}

// Returns the Swift type for map keys
func SwiftMapKeyType(desc protoreflect.FieldDescriptor) string {
	switch desc.Kind() {
	case protoreflect.StringKind:
		return swiftString
	case protoreflect.Int32Kind, protoreflect.Sint32Kind, protoreflect.Sfixed32Kind:
		return swiftInt32
	case protoreflect.Int64Kind, protoreflect.Sint64Kind, protoreflect.Sfixed64Kind:
		return swiftInt64
	case protoreflect.Uint32Kind, protoreflect.Fixed32Kind:
		return swiftUInt32
	case protoreflect.Uint64Kind, protoreflect.Fixed64Kind:
		return swiftUInt64
	case protoreflect.BoolKind:
		return swiftBool
	default:
		return swiftString
	}
}

// Returns the Swift type for map values
func SwiftMapValueType(field *protogen.Field) string {
	if field.Message != nil && len(field.Message.Fields) > 1 {
		// Map entry messages have exactly 2 fields: key and value
		for _, f := range field.Message.Fields {
			// Value field is always number 2 (double check this)
			if f.Desc.Number() == 2 {
				return SingleSwiftFieldType(f)
			}
		}
	}
	return swiftAny
}

// Return the Swift type for a single (non-repeated, non-map) protobuf field
func SingleSwiftFieldType(field *protogen.Field) string {
	switch field.Desc.Kind() {
	case protoreflect.StringKind:
		return swiftString
	case protoreflect.BoolKind:
		return swiftBool
	case protoreflect.Int32Kind, protoreflect.Sint32Kind, protoreflect.Sfixed32Kind:
		return swiftInt32
	case protoreflect.Int64Kind, protoreflect.Sint64Kind, protoreflect.Sfixed64Kind:
		return swiftInt64
	case protoreflect.Uint32Kind, protoreflect.Fixed32Kind:
		return swiftUInt32
	case protoreflect.Uint64Kind, protoreflect.Fixed64Kind:
		return swiftUInt64
	case protoreflect.FloatKind:
		return swiftFloat
	case protoreflect.DoubleKind:
		return swiftDouble
	case protoreflect.BytesKind:
		return swiftData
	case protoreflect.EnumKind:
		if field.Enum != nil {
			return field.Enum.GoIdent.GoName
		}
		return swiftInt32
	case protoreflect.MessageKind:
		if field.Message != nil {
			return field.Message.GoIdent.GoName
		}
		return swiftAny
	case protoreflect.GroupKind:
		if field.Message != nil {
			return field.Message.GoIdent.GoName
		}
		return swiftAny
	default:
		return swiftAny
	}
}

// Convert from Go to Swift parameter naming convention (e.g., "Method: Method" -> "method: Method")
func SwiftParamName(s string) string {
	if len(s) == 0 {
		return ""
	}
	return strings.ToLower(s[:1]) + s[1:]
}
