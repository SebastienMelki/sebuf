// Package swifthttpgen generates Swift HTTP client code for protobuf services.
package swifthttpgen

import (
	"strings"

	"google.golang.org/protobuf/compiler/protogen"
	"google.golang.org/protobuf/proto"

	"github.com/SebastienMelki/sebuf/http"
)

// Generator handles Swift HTTP client generation for protobuf services.
type Generator struct {
	plugin *protogen.Plugin
}

// New creates a new Swift HTTP generator.
func New(plugin *protogen.Plugin) *Generator {
	return &Generator{
		plugin: plugin,
	}
}

// Generate processes all files and generates Swift HTTP handlers.
func (g *Generator) Generate() error {
	for _, file := range g.plugin.Files {
		if !file.Generate {
			continue
		}
		if err := g.generateFile(file); err != nil {
			return err
		}
	}
	return nil
}

func (g *Generator) generateFile(file *protogen.File) error {
	if len(file.Services) == 0 {
		return nil
	}

	// Generate main HTTP File
	if err := g.generateHTTPFile(file); err != nil {
		return err
	}

	filename := file.GeneratedFilenamePrefix + "_http.swift"
	gf := g.plugin.NewGeneratedFile(filename, file.GoImportPath)

	g.writeHeader(gf)
	g.writeErrorTypes(gf)

	for _, service := range file.Services {
		if err := g.generateService(gf, service); err != nil {
			return err
		}
	}

	return nil
}

func (g *Generator) generateHTTPFile(file *protogen.File) error {
	filename := file.GeneratedFilenamePrefix + "_http.pb.swift"
	gf := g.plugin.NewGeneratedFile(filename, file.GoImportPath)

	g.writeHeader(gf, file)

	gf.P("import Foundation")
	gf.P("import SwiftProtobuf")
	gf.P()

	for _, service := range file.Services {
		if err := g.generateService(gf, file, service); err != nil {
			return err
		}
	}
}

func (g *Generator) writeHeader(gf *protogen.GeneratedFile, file *protogen.File) {
	gf.P("// Code generated by protoc-gen-swift-http. DO NOT EDIT.")
	gf.P("// source: ", file.Desc.Path())
	gf.P()
}

func (g *Generator) generateService(gf *protogen.GeneratedFile, file *protogen.File, service *protogen.Service) error {
	serviceName := service.GoName

	// Generate service protocol
	gf.P("/// ", serviceName, "Server is the service API for UserService server.")
	gf.P("protocol ", serviceName, " {")
	for _, method := range service.Methods {
		methodName := swiftMethodName((method.GoName))
		inputType := method.Input.GoIdent
		outputType := method.Output.GoIdent
		gf.P("func ", methodName, "(_ request: ", inputType, ") async throws -> ", outputType)
	}
	gf.P("}")
	gf.P()

	// Generate protocol
	g.generateServiceProtocol(gf, service)

	// Generate implementation
	g.generateServiceImplementation(gf, service)

	return nil
}

func (g *Generator) generateServiceProtocol(gf *protogen.GeneratedFile, service *protogen.Service) {
	serviceName := service.GoName

	gf.P()
	gf.P("/// Protocol for ", serviceName, " client operations")
	gf.P("protocol ", serviceName, "Client {")

	for _, method := range service.Methods {
		methodName := swiftMethodName(method.GoName)
		inputType := method.Input.GoIdent.GoName
		outputType := method.Output.GoIdent.GoName

		gf.P("    /// ", method.Comments.Leading.String())
		gf.P("    func ", methodName, "(_ request: ", inputType, ") async throws -> ", outputType)
	}

	gf.P("}")
	gf.P()
}

func (g *Generator) writeErrorTypes(gf *protogen.GeneratedFile) {
	gf.P("// MARK: - HTTP Client Error Types")
	gf.P()

	// Base protocol for all HTTP errors
	gf.P("/// Base protocol for all HTTP client errors")
	gf.P("protocol HTTPClientError: Error {")
	gf.P("    var message: String { get }")
	gf.P("    var statusCode: Int? { get }")
	gf.P("}")
	gf.P()

	// Network error
	gf.P("/// Network-related errors (connection, timeout, etc.)")
	gf.P("struct NetworkError: HTTPClientError {")
	gf.P("    let message: String")
	gf.P("    let statusCode: Int? = nil")
	gf.P("    let underlyingError: Error")
	gf.P("    ")
	gf.P("    init(_ underlyingError: Error) {")
	gf.P("        self.underlyingError = underlyingError")
	gf.P("        self.message = \"Network error: \\(underlyingError.localizedDescription)\"")
	gf.P("    }")
	gf.P("}")
	gf.P()

	// HTTP error
	gf.P("/// HTTP status code errors (4xx, 5xx)")
	gf.P("struct HTTPStatusError: HTTPClientError {")
	gf.P("    let message: String")
	gf.P("    let statusCode: Int?")
	gf.P("    let responseData: Data?")
	gf.P("    ")
	gf.P("    init(statusCode: Int, data: Data?) {")
	gf.P("        self.statusCode = statusCode")
	gf.P("        self.responseData = data")
	gf.P("        if let data = data, let responseString = String(data: data, encoding: .utf8) {")
	gf.P("            self.message = \"HTTP \\(statusCode): \\(responseString)\"")
	gf.P("        } else {")
	gf.P("            self.message = \"HTTP \\(statusCode): Unknown error\"")
	gf.P("        }")
	gf.P("    }")
	gf.P("}")
	gf.P()

	// Serialization error
	gf.P("/// JSON serialization/deserialization errors")
	gf.P("struct SerializationError: HTTPClientError {")
	gf.P("    let message: String")
	gf.P("    let statusCode: Int? = nil")
	gf.P("    let underlyingError: Error?")
	gf.P("    ")
	gf.P("    init(_ description: String, underlyingError: Error? = nil) {")
	gf.P("        self.message = \"Serialization error: \\(description)\"")
	gf.P("        self.underlyingError = underlyingError")
	gf.P("    }")
	gf.P("}")
	gf.P()

	// Invalid URL error
	gf.P("/// Invalid URL construction errors")
	gf.P("struct InvalidURLError: HTTPClientError {")
	gf.P("    let message: String")
	gf.P("    let statusCode: Int? = nil")
	gf.P("    ")
	gf.P("    init(_ url: String) {")
	gf.P("        self.message = \"Invalid URL: \\(url)\"")
	gf.P("    }")
	gf.P("}")
	gf.P()
}

func (g *Generator) generateServiceImplementation(gf *protogen.GeneratedFile, service *protogen.Service) {
	serviceName := service.GoName

	gf.P("// MARK: - ", serviceName, " HTTP Implementation")
	gf.P()
	gf.P("/// HTTP client implementation for ", serviceName)
	gf.P("class ", serviceName, "HTTPClient: ", serviceName, "Client {")
	gf.P("    private let baseURL: URL")
	gf.P("    private let session: URLSession")
	gf.P("    private let jsonOptions = JSONSerialization.WritingOptions()")
	gf.P("    ")
	gf.P("    /// Initialize HTTP client with base URL")
	gf.P("    /// - Parameters:")
	gf.P("    ///   - baseURL: Base URL for the service")
	gf.P("    ///   - session: URLSession to use (defaults to .shared)")
	gf.P("    init(baseURL: URL, session: URLSession = .shared) {")
	gf.P("        self.baseURL = baseURL")
	gf.P("        self.session = session")
	gf.P("    }")
	gf.P("    ")

	// Generate methods
	for _, method := range service.Methods {
		g.generateServiceMethod(gf, service, method)
	}

	// Generate helper methods
	g.generateHTTPHelpers(gf)

	gf.P("}")
	gf.P()
}

func (g *Generator) generateServiceMethod(gf *protogen.GeneratedFile, service *protogen.Service, method *protogen.Method) {
	methodName := swiftMethodName(method.GoName)
	inputType := method.Input.GoIdent.GoName
	outputType := method.Output.GoIdent.GoName

	// Get HTTP annotation
	httpRule := g.getHTTPRule(method)
	httpMethod := "POST" // default
	path := "/" + strings.ToLower(service.GoName) + "/" + strings.ToLower(method.GoName)

	if httpRule != nil {
		switch httpRule.Pattern.(type) {
		case *http.HttpRule_Get:
			httpMethod = "GET"
			path = httpRule.GetGet()
		case *http.HttpRule_Post:
			httpMethod = "POST"
			path = httpRule.GetPost()
		case *http.HttpRule_Put:
			httpMethod = "PUT"
			path = httpRule.GetPut()
		case *http.HttpRule_Delete:
			httpMethod = "DELETE"
			path = httpRule.GetDelete()
		case *http.HttpRule_Patch:
			httpMethod = "PATCH"
			path = httpRule.GetPatch()
		}
	}

	gf.P("    /// ", method.Comments.Leading.String())
	gf.P("    func ", methodName, "(_ request: ", inputType, ") async throws -> ", outputType, " {")
	gf.P("        let path = \"", path, "\"")
	gf.P("        guard let url = URL(string: path, relativeTo: baseURL) else {")
	gf.P("            throw InvalidURLError(\"\\(baseURL)/\\(path)\")")
	gf.P("        }")
	gf.P("        ")
	gf.P("        var urlRequest = URLRequest(url: url)")
	gf.P("        urlRequest.httpMethod = \"", httpMethod, "\"")
	gf.P("        urlRequest.setValue(\"application/json\", forHTTPHeaderField: \"Content-Type\")")
	gf.P("        urlRequest.setValue(\"application/json\", forHTTPHeaderField: \"Accept\")")
	gf.P("        ")

	if httpMethod != "GET" {
		gf.P("        // Serialize request to JSON")
		gf.P("        do {")
		gf.P("            let requestData = try request.jsonUTF8Data()")
		gf.P("            urlRequest.httpBody = requestData")
		gf.P("        } catch {")
		gf.P("            throw SerializationError(\"Failed to serialize request\", underlyingError: error)")
		gf.P("        }")
		gf.P("        ")
	}

	gf.P("        return try await performRequest(urlRequest, responseType: ", outputType, ".self)")
	gf.P("    }")
	gf.P("    ")
}

func (g *Generator) generateHTTPHelpers(gf *protogen.GeneratedFile) {
	gf.P("    // MARK: - Private HTTP Helpers")
	gf.P("    ")
	gf.P("    /// Perform HTTP request and decode response")
	gf.P("    private func performRequest<T: SwiftProtobuf.Message>(_ request: URLRequest, responseType: T.Type) async throws -> T {")
	gf.P("        let (data, response) = try await performHTTPRequest(request)")
	gf.P("        ")
	gf.P("        // Decode JSON response")
	gf.P("        do {")
	gf.P("            return try T(jsonUTF8Data: data)")
	gf.P("        } catch {")
	gf.P("            throw SerializationError(\"Failed to decode response\", underlyingError: error)")
	gf.P("        }")
	gf.P("    }")
	gf.P("    ")
	gf.P("    /// Perform raw HTTP request with error handling")
	gf.P("    private func performHTTPRequest(_ request: URLRequest) async throws -> (Data, URLResponse) {")
	gf.P("        do {")
	gf.P("            let (data, response) = try await session.data(for: request)")
	gf.P("            ")
	gf.P("            // Check HTTP status code")
	gf.P("            if let httpResponse = response as? HTTPURLResponse {")
	gf.P("                switch httpResponse.statusCode {")
	gf.P("                case 200...299:")
	gf.P("                    return (data, response)")
	gf.P("                default:")
	gf.P("                    throw HTTPStatusError(statusCode: httpResponse.statusCode, data: data)")
	gf.P("                }")
	gf.P("            }")
	gf.P("            ")
	gf.P("            return (data, response)")
	gf.P("        } catch let error as HTTPClientError {")
	gf.P("            throw error")
	gf.P("        } catch {")
	gf.P("            throw NetworkError(error)")
	gf.P("        }")
	gf.P("    }")
}

// getHTTPRule extracts HTTP rule from method options
func (g *Generator) getHTTPRule(method *protogen.Method) *http.HttpRule {
	if method.Desc.Options() == nil {
		return nil
	}

	if !proto.HasExtension(method.Desc.Options(), http.E_Http) {
		return nil
	}

	ext := proto.GetExtension(method.Desc.Options(), http.E_Http)
	if httpRule, ok := ext.(*http.HttpRule); ok {
		return httpRule
	}

	return nil
}

// swiftMethodName converts a Go method name to Swift method name convention.
func swiftMethodName(goName string) string {
	if len(goName) == 0 {
		return goName
	}

	// Convert from Go method name (e.g., "CreateUser") to Swift method name (e.g., "createUser")
	return strings.ToLower(goName[:1]) + goName[1:]
}
