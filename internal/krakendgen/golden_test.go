// TestKrakenDValidationErrors — expected failures
// ---------------------------------------------------------------------------

func TestKrakenDValidationErrors(t *testing.T) {
	if _, err := exec.LookPath("protoc"); err != nil {
		t.Skip("protoc not found, skipping validation error tests")
	}

	baseDir, err := os.Getwd()
	if err != nil {
		t.Fatalf("Failed to get working directory: %v", err)
	}

	projectRoot := filepath.Join(baseDir, "..", "..")
	protoDir := filepath.Join(baseDir, "testdata", "proto")
	pluginPath := filepath.Join(projectRoot, "bin", "protoc-gen-krakend")

	// Build plugin if needed.
	if _, statErr := os.Stat(pluginPath); os.IsNotExist(statErr) {
		buildCmd := exec.Command("make", "build")
		buildCmd.Dir = projectRoot
		if buildErr := buildCmd.Run(); buildErr != nil {
			t.Fatalf("Failed to build plugin: %v", buildErr)
		}
	}

	testCases := []struct {
		name      string
		protoFile string
		wantErr   string // substring expected in stderr
	}{
		{
			name:      "duplicate routes",
			protoFile: "invalid_duplicate_routes.proto",
			wantErr:   "duplicate route: GET /api/v1/users",
		},
		{
			name:      "static vs param conflict",
			protoFile: "invalid_route_conflict.proto",
			wantErr:   "route conflict",
		},
		{
			name:      "missing gateway_config",
			protoFile: "invalid_no_host.proto",
			wantErr:   "has no (sebuf.krakend.gateway_config) annotation",
		},
		{
			name:      "invalid circuit breaker",
			protoFile: "invalid_circuit_breaker.proto",
			wantErr:   "circuit_breaker requires",
		},
		{
			name:      "invalid cache mismatched max_items/max_size",
			protoFile: "invalid_cache.proto",
			wantErr:   "max_items and max_size must both be set",
		},
		{
			name:      "rate limit HEADER strategy missing key",
			protoFile: "invalid_rate_limit_key.proto",
			wantErr:   "requires a key",
		},
	}

	for _, tc := range testCases {
		t.Run(tc.name, func(t *testing.T) {
			tempDir := t.TempDir()

			cmd := exec.Command("protoc",
				"--plugin=protoc-gen-krakend="+pluginPath,
				"--krakend_out="+tempDir,
				"--proto_path="+protoDir,
				"--proto_path="+filepath.Join(projectRoot, "proto"),
				tc.protoFile,
			)

			var stderr bytes.Buffer
			cmd.Stderr = &stderr

			runErr := cmd.Run()
			if runErr == nil {
				t.Fatalf("expected protoc to fail for %s, but it succeeded", tc.protoFile)
			}

			stderrStr := stderr.String()
			if !strings.Contains(stderrStr, tc.wantErr) {
				t.Errorf("expected stderr to contain %q, got:\n%s", tc.wantErr, stderrStr)
			}
		})
	}
}

// ---------------------------------------------------------------------------
// TestKrakenDSchemaValidation — krakend check -lc on all golden files
// ---------------------------------------------------------------------------

func TestKrakenDSchemaValidation(t *testing.T) {
	krakendPath, err := exec.LookPath("krakend")
	if err != nil {
		t.Skip("krakend CLI not found in PATH, skipping schema validation")
	}

	baseDir, err := os.Getwd()
	if err != nil {
		t.Fatalf("Failed to get working directory: %v", err)
	}

	goldenDir := filepath.Join(baseDir, "testdata", "golden")
	entries, err := os.ReadDir(goldenDir)
	if err != nil {
		t.Fatalf("Failed to read golden dir: %v", err)
	}

	for _, entry := range entries {
		if !strings.HasSuffix(entry.Name(), ".krakend.json") {
			continue
		}
		t.Run(entry.Name(), func(t *testing.T) {
			filePath := filepath.Join(goldenDir, entry.Name())
			// Default: syntax-only check (-c). Schema lint (-lc) requires
			// fetching from krakend.io and can timeout in CI or offline.
			// Set KRAKEND_LINT=1 to enable full online schema validation.
			args := []string{"check", "-c", filePath}
			if os.Getenv("KRAKEND_LINT") == "1" {
				args = []string{"check", "-lc", filePath}
			}
			cmd := exec.Command(krakendPath, args...)
			var stderr bytes.Buffer
			cmd.Stderr = &stderr
			if runErr := cmd.Run(); runErr != nil {
				t.Errorf("krakend check failed for %s: %v\n%s",
					entry.Name(), runErr, stderr.String())
			}
		})
	}
}
