package tsservergen

import (
	"google.golang.org/protobuf/compiler/protogen"

	"github.com/SebastienMelki/sebuf/internal/tscommon"
)

// generateModules implements import_style=modules: shared canonical type modules
// and an errors module (via tscommon), plus one slimmed server module per
// service file that imports its request/response types and the error helpers.
func (g *Generator) generateModules() error {
	if err := tscommon.EmitSharedModules(g.plugin, g.opts); err != nil {
		return err
	}
	for _, file := range g.plugin.Files {
		if !file.Generate || len(file.Services) == 0 {
			continue
		}
		g.emitServerModule(file)
	}
	return nil
}

// emitServerModule writes a service file's server framework types + handlers,
// importing the request/response types from their canonical modules and the
// shared error helpers.
func (g *Generator) emitServerModule(file *protogen.File) {
	module := file.GeneratedFilenamePrefix + "_server"
	gf := g.plugin.NewGeneratedFile(module+".ts", "")
	tracker := tscommon.NewImportTracker()
	g.ctx = &tscommon.EmitContext{Options: g.opts, SelfModule: module, Imports: tracker}
	defer func() { g.ctx = nil }()

	// Server framework types and validation helpers always reference the shared
	// error helpers (FieldViolation/ValidationError).
	g.ctx.NeedErrors()

	var body []string
	bp := tscommon.BufferedPrinter(&body)

	g.writeServerTypes(bp)
	if g.fileUsesHeaders(file) {
		g.writeHeaderValidationHelpers(bp)
	}
	for _, service := range file.Services {
		_ = g.generateService(bp, service)
	}

	dp := tscommon.DirectPrinter(gf)
	dp("// Code generated by protoc-gen-ts-server. DO NOT EDIT.")
	dp("// source: %s", file.Desc.Path())
	dp("")
	tracker.Render(dp)
	for _, line := range body {
		gf.P(line)
	}
}
