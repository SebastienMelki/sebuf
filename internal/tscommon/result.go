package tscommon

import (
	"sort"
	"strings"

	"google.golang.org/protobuf/compiler/protogen"
)

// resultModule is the extensionless module path of the shared Result/typed-error
// helper file emitted at the output root when ts_error_handling=result is active
// (protobuf-es mode only).
const resultModule = "result"

// RefResult imports a symbol from the shared result module (result.ts) into the
// file being written and returns its local name. typeOnly selects an
// `import type` (Result, ClientError) vs a value import (decodeError).
func (c *EmitContext) RefResult(symbol string, typeOnly bool) string {
	spec := RelativeImportSpecifier(c.SelfModule, resultModule)
	if typeOnly {
		return c.Imports.NeedType(spec, symbol)
	}
	return c.Imports.NeedValue(spec, symbol)
}

// collectErrorMessages returns the proto-defined "*Error" messages across all
// generated files, sorted by full name for deterministic output. Mirrors the
// naming convention used by CollectAllServiceMessages and the Python client:
// any top-level message whose name ends in "Error".
func collectErrorMessages(plugin *protogen.Plugin) []*protogen.Message {
	var out []*protogen.Message
	for _, file := range plugin.Files {
		if !file.Generate {
			continue
		}
		for _, msg := range file.Messages {
			if strings.HasSuffix(string(msg.Desc.Name()), "Error") {
				out = append(out, msg)
			}
		}
	}
	sort.Slice(out, func(i, j int) bool {
		return out[i].Desc.FullName() < out[j].Desc.FullName()
	})
	return out
}

// EmitResultModule emits the shared result.ts module: the generic Result type,
// the ClientError union (ValidationError | ApiError | every proto *Error type),
// a structural error registry, and decodeError. It is emitted only in
// protobuf-es + ts_error_handling=result mode and is imported by the generated
// clients. The typed-error decode mirrors the Python client's _ERROR_CLASSES
// registry (internal/pyclientgen): first entry whose required JSON keys are all
// present in the response body wins.
func EmitResultModule(plugin *protogen.Plugin) {
	errs := collectErrorMessages(plugin)

	tracker := NewImportTracker()
	ctx := &EmitContext{
		SelfModule:     resultModule,
		Imports:        tracker,
		MessageRuntime: MessageRuntimeES,
		ErrorHandling:  ErrorHandlingResult,
	}

	// Reference each proto *Error's type + runtime schema, recording imports.
	refs := make([]resultErrorRef, 0, len(errs))
	for _, msg := range errs {
		keys := make([]string, 0, len(msg.Fields))
		for _, f := range msg.Fields {
			keys = append(keys, f.Desc.JSONName())
		}
		refs = append(refs, resultErrorRef{
			typeName:   ctx.RefMessagePb(msg),
			schemaName: ctx.RefMessageSchema(msg),
			keys:       keys,
		})
	}

	// fromJson (value) + DescMessage (type) from @bufbuild/protobuf are only
	// needed when there are proto *Error messages to decode through the registry.
	if len(refs) > 0 {
		tracker.NeedProtobufES("fromJson", "DescMessage")
	}
	tracker.NeedErrors(RelativeImportSpecifier(resultModule, errorsModule), "ApiError", "ValidationError")

	var body []string
	bp := BufferedPrinter(&body)
	writeResultBody(bp, refs)

	gf := plugin.NewGeneratedFile(resultModule+".ts", "")
	dp := DirectPrinter(gf)
	dp("// Code generated by sebuf. DO NOT EDIT.")
	dp("")
	tracker.Render(dp)
	for _, line := range body {
		gf.P(line)
	}
}

// resultErrorRef holds the local (possibly aliased) TypeScript type name and
// runtime schema handle for one proto *Error, plus its JSON marker keys.
type resultErrorRef struct {
	typeName   string
	schemaName string
	keys       []string
}

// writeResultBody prints the Result type, ClientError union, error registry, and
// decodeError, given the local names/keys of every proto *Error.
func writeResultBody(p Printer, refs []resultErrorRef) {
	p("// Discriminated result of an RPC call: exactly one of `data` / `error` is")
	p("// set, narrowable on the `ok` discriminant or by checking `error`.")
	p("export type Result<T, E> =")
	p("  | { ok: true; data: T; error?: undefined }")
	p("  | { ok: false; data?: undefined; error: E };")
	p("")

	// ClientError union: built-ins + every proto *Error type.
	union := []string{"ValidationError", "ApiError"}
	for _, r := range refs {
		union = append(union, r.typeName)
	}
	p("// Every error an RPC can surface: the two built-ins plus each proto-defined")
	p("// *Error message. Proto errors carry protobuf-es's `$typeName` discriminant.")
	p("export type ClientError = %s;", strings.Join(union, " | "))
	p("")

	hasErrors := len(refs) > 0
	if hasErrors {
		// Structural registry: [schema, requiredJsonKeys].
		p("// [schema, required JSON keys] — decodeError returns the first proto *Error")
		p("// whose keys are all present in the response body (mirrors the Python client).")
		p("const ERROR_REGISTRY: [DescMessage, string[]][] = [")
		for _, r := range refs {
			p("  [%s, [%s]],", r.schemaName, quoteJoin(r.keys))
		}
		p("];")
		p("")
	}

	p("// Decode a non-ok Response into a typed ClientError (never throws).")
	p("export async function decodeError(resp: Response): Promise<ClientError> {")
	p("  const body = await resp.text();")
	p("  if (resp.status === 400) {")
	p("    try {")
	p("      const parsed = JSON.parse(body);")
	p("      if (parsed.violations) {")
	p("        return new ValidationError(parsed.violations);")
	p("      }")
	p("    } catch {")
	p("      // fall through to a proto *Error / ApiError")
	p("    }")
	p("  }")
	if hasErrors {
		p("  try {")
		p("    const json = JSON.parse(body);")
		p("    for (const [schema, keys] of ERROR_REGISTRY) {")
		p("      if (keys.every((k) => k in json)) {")
		p("        return fromJson(schema, json, { ignoreUnknownFields: true }) as ClientError;")
		p("      }")
		p("    }")
		p("  } catch {")
		p("    // body was not JSON; fall through to ApiError")
		p("  }")
	}
	p("  return new ApiError(resp.status, `Request failed with status ${resp.status}`, body);")
	p("}")
	p("")
}

// quoteJoin renders a []string as a comma-separated list of double-quoted TS
// string literals.
func quoteJoin(ss []string) string {
	parts := make([]string, len(ss))
	for i, s := range ss {
		parts[i] = `"` + s + `"`
	}
	return strings.Join(parts, ", ")
}
