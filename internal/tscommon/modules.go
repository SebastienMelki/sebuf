package tscommon

import (
	"fmt"
	"sort"
	"strings"

	"google.golang.org/protobuf/compiler/protogen"
)

// reservedTypeNames are the symbols emitted by the shared errors module. A proto
// message with one of these names would shadow the helpers in modules mode.
var reservedTypeNames = map[string]bool{
	"ValidationError": true,
	"ApiError":        true,
	"FieldViolation":  true,
}

// BufferedPrinter returns a Printer that accumulates formatted lines into *lines.
func BufferedPrinter(lines *[]string) Printer {
	return func(format string, args ...interface{}) {
		if len(args) == 0 {
			*lines = append(*lines, format)
		} else {
			*lines = append(*lines, fmt.Sprintf(format, args...))
		}
	}
}

// DirectPrinter returns a Printer that writes formatted lines straight to gf.
func DirectPrinter(gf *protogen.GeneratedFile) Printer {
	return func(format string, args ...interface{}) {
		if len(args) == 0 {
			gf.P(format)
		} else {
			gf.P(fmt.Sprintf(format, args...))
		}
	}
}

// CollectAllServiceMessages builds the transitive closure of every message/enum
// referenced by any service across all generated files, plus proto-defined
// "*Error" messages (matching CollectServiceMessages).
func CollectAllServiceMessages(plugin *protogen.Plugin) *MessageSet {
	ms := NewMessageSet()
	for _, file := range plugin.Files {
		if !file.Generate {
			continue
		}
		for _, service := range file.Services {
			for _, method := range service.Methods {
				ms.AddMessage(method.Input)
				ms.AddMessage(method.Output)
			}
		}
		for _, msg := range file.Messages {
			if strings.HasSuffix(string(msg.Desc.Name()), "Error") {
				ms.AddMessage(msg)
			}
		}
	}
	return ms
}

// CheckReservedNames returns an error if any collected message would shadow the
// shared error helpers in modules mode.
func CheckReservedNames(ms *MessageSet) error {
	for _, msg := range ms.OrderedMessages() {
		if reservedTypeNames[string(msg.Desc.Name())] {
			return fmt.Errorf(
				"message %q collides with a reserved error-helper name in import_style=modules; rename it",
				msg.Desc.FullName(),
			)
		}
	}
	return nil
}

// EmitSharedModules emits, for import_style=modules, one canonical type module
// per proto file (<proto>.ts) plus a single shared errors module (errors.ts).
// Both TS generators call this; the emitted files are byte-identical between
// them (neutral header), so running client and server against the same output
// directory yields consistent type modules.
func EmitSharedModules(plugin *protogen.Plugin, opts Options) error {
	global := CollectAllServiceMessages(plugin)
	if err := CheckReservedNames(global); err != nil {
		return err
	}

	msgsBySrc := global.MessagesBySourceFile()
	enumsBySrc := global.EnumsBySourceFile()
	for _, src := range sortedSourceFiles(msgsBySrc, enumsBySrc) {
		emitTypeModule(plugin, opts, src, msgsBySrc[src], enumsBySrc[src])
	}
	emitErrorsModule(plugin)
	return nil
}

func sortedSourceFiles(msgs map[string][]*protogen.Message, enums map[string][]*protogen.Enum) []string {
	seen := map[string]bool{}
	var srcs []string
	for src := range msgs {
		if !seen[src] {
			seen[src] = true
			srcs = append(srcs, src)
		}
	}
	for src := range enums {
		if !seen[src] {
			seen[src] = true
			srcs = append(srcs, src)
		}
	}
	sort.Strings(srcs)
	return srcs
}

func emitTypeModule(
	plugin *protogen.Plugin,
	opts Options,
	srcPath string,
	msgs []*protogen.Message,
	enums []*protogen.Enum,
) {
	if len(msgs) == 0 && len(enums) == 0 {
		return
	}
	module := ModuleForFile(srcPath)
	gf := plugin.NewGeneratedFile(module+".ts", "")
	tracker := NewImportTracker()
	ctx := &EmitContext{Options: opts, SelfModule: module, Imports: tracker}

	var body []string
	bp := BufferedPrinter(&body)
	for _, msg := range msgs {
		GenerateInterfaceCtx(ctx, bp, msg)
	}
	for _, enum := range enums {
		GenerateEnumType(bp, enum)
	}

	dp := DirectPrinter(gf)
	dp("// Code generated by sebuf. DO NOT EDIT.")
	dp("// source: %s", srcPath)
	dp("")
	tracker.Render(dp)
	for _, line := range body {
		gf.P(line)
	}
}

func emitErrorsModule(plugin *protogen.Plugin) {
	gf := plugin.NewGeneratedFile("errors.ts", "")
	dp := DirectPrinter(gf)
	dp("// Code generated by sebuf. DO NOT EDIT.")
	dp("")
	WriteErrorTypes(dp)
}
