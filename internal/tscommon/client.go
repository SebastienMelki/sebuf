package tscommon

import "google.golang.org/protobuf/compiler/protogen"

// clientModule is the extensionless module path of the shared client-runtime
// module (client.ts), emitted in protobuf-es mode. It holds the base
// RequestOptions interface shared by every generated standalone RPC function.
const clientModule = "client"

// RefClient imports a symbol from the shared client module (client.ts) into the
// file being written and returns its local name. typeOnly selects an
// `import type` (RequestOptions) over a value import.
func (c *EmitContext) RefClient(symbol string, typeOnly bool) string {
	spec := RelativeImportSpecifier(c.SelfModule, clientModule)
	if typeOnly {
		return c.Imports.NeedType(spec, symbol)
	}
	return c.Imports.NeedValue(spec, symbol)
}

// EmitClientModule emits the shared client.ts module: the base RequestOptions
// interface passed to every standalone protobuf-es RPC function. Services that
// declare typed headers extend this interface with their header properties;
// services without headers reference it directly. Emitted once per invocation
// in protobuf-es mode (both throw and Result error handling).
func EmitClientModule(plugin *protogen.Plugin) {
	gf := plugin.NewGeneratedFile(clientModule+".ts", "")
	dp := DirectPrinter(gf)
	dp("// Code generated by sebuf. DO NOT EDIT.")
	dp("")
	dp("// Per-call configuration for a generated protobuf-es RPC function. `baseURL`")
	dp("// is required; the rest are optional. Services that declare typed headers")
	dp("// extend this with their header properties.")
	dp("export interface RequestOptions {")
	dp("  baseURL: string;")
	dp("  fetch?: typeof fetch;")
	dp("  headers?: Record<string, string>;")
	dp("  signal?: AbortSignal;")
	dp("}")
}
