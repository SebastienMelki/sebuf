func (g *Generator) generateSystemTextRootUnwrapNormalizationBody(
	gf *protogen.GeneratedFile,
	message *contractmodel.Message,
	messageIndex map[string]*contractmodel.Message,
	serialize bool,
) {
	if len(message.Fields) == 0 {
		gf.P("            return token;")
		return
	}
	field := message.Fields[0]
	if field.Repeated {
		if ref := enumTypeRef(field); ref != nil && needsEnumEncodingNormalization(field) {
			gf.P("            if (token is not JsonArray array)")
			gf.P("            {")
			gf.P("                return token;")
			gf.P("            }")
			gf.P("            for (var i = 0; i < array.Count; i++)")
			gf.P("            {")
			g.generateSystemTextEnumNodeAssignment(gf, "array[i]", ref.Name, serialize, "                ")
			gf.P("            }")
			gf.P("            return array;")
			return
		}
		if needsBytesEncodingNormalization(field) {
			gf.P("            if (token is not JsonArray array)")
			gf.P("            {")
			gf.P("                return token;")
			gf.P("            }")
			gf.P("            for (var i = 0; i < array.Count; i++)")
			gf.P("            {")
			g.generateSystemTextBytesNodeAssignment(gf, "array[i]", field, serialize, "                ")
			gf.P("            }")
			gf.P("            return array;")
			return
		}
		if field.Type == nil || field.Type.Kind != contractmodel.KindMessage ||
			!messageNeedsJSONNormalization(messageForRef(messageIndex, field.Type), messageIndex) {
			gf.P("            return token;")
			return
		}
		gf.P("            if (token is not JsonArray array)")
		gf.P("            {")
		gf.P("                return token;")
		gf.P("            }")
		gf.P("            for (var i = 0; i < array.Count; i++)")
		gf.P("            {")
		gf.P("                if (array[i] is not JsonNode item)")
		gf.P("                {")
		gf.P("                    continue;")
		gf.P("                }")
		if serialize {
			gf.P("                array[i] = NormalizeSerializedNode(typeof(", g.csharpNamedType(field.Type), "), item);")
		} else {
			gf.P("                array[i] = NormalizeResponseNode(typeof(", g.csharpNamedType(field.Type), "), item);")
		}
		gf.P("            }")
		gf.P("            return array;")
		return
	}
	if !field.IsMap || field.Type == nil || field.Type.MapValue == nil {
		gf.P("            return token;")
		return
	}
	if ref := enumTypeRef(field); ref != nil && needsEnumEncodingNormalization(field) {
		gf.P("            if (token is not JsonObject obj)")
		gf.P("            {")
		gf.P("                return token;")
		gf.P("            }")
		gf.P("            foreach (var key in obj.Select(pair => pair.Key).ToList())")
		gf.P("            {")
		g.generateSystemTextEnumNodeAssignment(gf, "obj[key]", ref.Name, serialize, "                ")
		gf.P("            }")
		gf.P("            return obj;")
		return
	}
	if needsBytesEncodingNormalization(field) {
		gf.P("            if (token is not JsonObject obj)")
		gf.P("            {")
		gf.P("                return token;")
		gf.P("            }")
		gf.P("            foreach (var key in obj.Select(pair => pair.Key).ToList())")
		gf.P("            {")
		g.generateSystemTextBytesNodeAssignment(gf, "obj[key]", field, serialize, "                ")
		gf.P("            }")
		gf.P("            return obj;")
		return
	}
	childMessage := field.Type.MapValue.Kind == contractmodel.KindMessage &&
		messageNeedsJSONNormalization(messageForRef(messageIndex, field.Type.MapValue), messageIndex)
	if !childMessage {
		gf.P("            return token;")
		return
	}
	gf.P("            if (token is not JsonObject obj)")
	gf.P("            {")
	gf.P("                return token;")
	gf.P("            }")
	gf.P("            foreach (var key in obj.Select(pair => pair.Key).ToList())")
	gf.P("            {")
	if serialize {
		gf.P("                if (obj[key] is JsonNode value)")
		gf.P("                {")
		if mapValueUsesUnwrap(messageForRef(messageIndex, field.Type.MapValue)) {
			gf.P(
				"                    obj[key] = NormalizeMapValueForSerialization(",
				"value, typeof(", g.csharpNamedType(field.Type.MapValue), "));",
			)
		} else {
			gf.P(
				"                    obj[key] = NormalizeSerializedNode(",
				"typeof(", g.csharpNamedType(field.Type.MapValue), "), value);",
			)
		}
		gf.P("                }")
	} else {
		gf.P("                if (obj[key] is JsonNode value)")
		gf.P("                {")
		if mapValueUsesUnwrap(messageForRef(messageIndex, field.Type.MapValue)) {
			gf.P(
				"                    obj[key] = NormalizeMapValueForResponse(",
				"value, typeof(", g.csharpNamedType(field.Type.MapValue), "));",
			)
		} else {
			gf.P(
				"                    obj[key] = NormalizeResponseNode(",
				"typeof(", g.csharpNamedType(field.Type.MapValue), "), value);",
			)
		}
		gf.P("                }")
	}
	gf.P("            }")
	gf.P("            return obj;")
}

