type Options struct {
	Namespace string
	JSONLib   string
}

type Generator struct {
	plugin               *protogen.Plugin
	opts                 Options
	packages             []*contractmodel.Package
	currentPackage       string
	usePackageNamespaces bool
}

func New(plugin *protogen.Plugin, opts Options) *Generator {
	return &Generator{plugin: plugin, opts: opts}
}

func NewOptions() (protogen.Options, *Options) {
	var flags flag.FlagSet
	cfg := &Options{}
	flags.StringVar(&cfg.Namespace, "namespace", "Sebuf.Generated", "C# namespace")
	flags.StringVar(&cfg.JSONLib, "json_lib", "newtonsoft", "JSON annotation library: newtonsoft or system_text_json")
	return protogen.Options{ParamFunc: flags.Set}, cfg
}

func (g *Generator) Generate() error {
	g.packages = contractmodel.Packages(g.plugin.Files)
	g.usePackageNamespaces = len(g.packages) > 1 || packagesUseCrossPackageTypes(g.packages)
	for _, pkg := range g.packages {
		if err := g.generatePackage(pkg); err != nil {
			return err
		}
	}
	return nil
}

func (g *Generator) generatePackage(pkg *contractmodel.Package) error {
	g.currentPackage = pkg.Name
	messages := g.packageMessages(pkg)
	messageIndex := g.messageIndex(pkg, messages)

	packagePath := strings.ReplaceAll(pkg.Name, ".", "/")
	gf := g.plugin.NewGeneratedFile(path.Join(packagePath, "Contracts.g.cs"), "")
	gf.P("// Code generated by protoc-gen-csharp-http. DO NOT EDIT.")
	gf.P("#nullable enable")
	gf.P("using System;")
	gf.P("using System.Collections.Generic;")
	gf.P("using System.Globalization;")
	gf.P("using System.Linq;")
	gf.P("using System.Net.Http;")
	gf.P("using System.Runtime.Serialization;")
	gf.P("using System.Text;")
	gf.P("using System.Threading;")
	gf.P("using System.Threading.Tasks;")
	if g.useNewtonsoft() {
		gf.P("using Newtonsoft.Json;")
		gf.P("using Newtonsoft.Json.Converters;")
		gf.P("using Newtonsoft.Json.Linq;")
	} else {
		gf.P("using System.Text.Json;")
		gf.P("using System.Text.Json.Serialization;")
		gf.P("using System.Text.Json.Nodes;")
	}
	gf.P()
	gf.P("namespace ", g.packageNamespace(pkg.Name))
	gf.P("{")
	g.generateEnums(gf, pkg.Enums)
	g.generateAPIException(gf)
	g.generateMessages(gf, messages, messageIndex)
	g.generateTypedErrorExceptions(gf, messages, messageIndex)
	g.generateServiceClients(gf, pkg.Services, messageIndex)
	g.generateServices(gf, pkg.Services)
	gf.P("}")
	return nil
}

func (g *Generator) messageIndex(
	pkg *contractmodel.Package,
	packageMessages []*contractmodel.Message,
) map[string]*contractmodel.Message {
	index := make(map[string]*contractmodel.Message)
	for _, candidate := range g.packages {
		for _, message := range candidate.Messages {
			index[typeKey(candidate.Name, message.Name)] = message
		}
	}
	for _, message := range packageMessages {
		index[message.Name] = message
		index[typeKey(pkg.Name, message.Name)] = message
	}
	return index
}

func typeKey(pkg, name string) string {
	if pkg == "" {
		return name
	}
	return pkg + "\x00" + name
}

func messageForRef(
	messageIndex map[string]*contractmodel.Message,
	ref *contractmodel.TypeRef,
) *contractmodel.Message {
	if ref == nil {
		return nil
	}
	if message := messageIndex[typeKey(ref.Package, ref.Name)]; message != nil {
		return message
	}
	return messageIndex[ref.Name]
}

func (g *Generator) packageNamespace(pkg string) string {
	if !g.usePackageNamespaces {
		return g.opts.Namespace
	}
	parts := strings.Split(pkg, ".")
	for i, part := range parts {
		parts[i] = csharpNamespaceSegment(part)
	}
	return g.opts.Namespace + "." + strings.Join(parts, ".")
}

func csharpNamespaceSegment(value string) string {
	segment := pascalCase(value)
	if segment == "" {
		return "Default"
	}
	if segment[0] >= '0' && segment[0] <= '9' {
		return "_" + segment
	}
	return segment
}

func packagesUseCrossPackageTypes(packages []*contractmodel.Package) bool {
	for _, pkg := range packages {
		for _, message := range pkg.Messages {
			for _, field := range message.Fields {
				if typeRefUsesOtherPackage(field.Type, pkg.Name) {
					return true
				}
			}
		}
		for _, service := range pkg.Services {
			for _, method := range service.Methods {
				if isUserProtoPackage(method.InputPackage) && method.InputPackage != pkg.Name ||
					isUserProtoPackage(method.ResponsePackage) && method.ResponsePackage != pkg.Name {
					return true
				}
			}
		}
	}
	return false
}

func isUserProtoPackage(pkg string) bool {
	return pkg != "" && pkg != "google.protobuf"
}

func typeRefUsesOtherPackage(ref *contractmodel.TypeRef, currentPackage string) bool {
	if ref == nil {
		return false
	}
	if ref.Package != "" && ref.Package != currentPackage {
		return true
	}
	return typeRefUsesOtherPackage(ref.MapKey, currentPackage) ||
		typeRefUsesOtherPackage(ref.MapValue, currentPackage)
}

type generatedProperty struct {
	jsonName    string
	name        string
	typ         string
	converter   string
	initializer string
}

func (g *Generator) messageProperties(
	message *contractmodel.Message,
	messageIndex map[string]*contractmodel.Message,
) []generatedProperty {
	var properties []generatedProperty
	usedJSONNames := make(map[string]bool)
	skippedFields := make(map[string]bool)

	g.markOneofFields(message, skippedFields)
	g.appendStandardMessageProperties(&properties, usedJSONNames, skippedFields, message, messageIndex)
	g.appendOneofProperties(&properties, usedJSONNames, message, messageIndex)

	return properties
}

func (g *Generator) generateEnums(gf *protogen.GeneratedFile, enums []*contractmodel.Enum) {
	for _, enum := range enums {
		gf.P("    public enum ", enum.Name)
		gf.P("    {")
		for _, value := range enum.Values {
			gf.P(`        [EnumMember(Value = "`, value.JSONValue, `")]`)
			gf.P("        ", pascalCase(value.Name), " = ", value.Number, ",")
		}
		gf.P("    }")
		gf.P()
	}
}

func (g *Generator) generateMessages(
	gf *protogen.GeneratedFile,
	messages []*contractmodel.Message,
	messageIndex map[string]*contractmodel.Message,
) {
	for _, message := range messages {
		if isRootUnwrapMessage(message) {
			gf.P("    public sealed class ", message.Name, " : ", g.rootUnwrapBaseType(message))
			gf.P("    {")
			gf.P("    }")
			gf.P()
			continue
		}

		gf.P("    public sealed class ", message.Name)
		gf.P("    {")
		for _, property := range g.messageProperties(message, messageIndex) {
			gf.P("        ", g.jsonAttribute(property.jsonName))
			if property.converter != "" {
				gf.P("        ", property.converter)
			}
			if property.initializer != "" {
				gf.P(
					"        public ", property.typ, " ", property.name,
					" { get; set; } = ", property.initializer, ";",
				)
			} else {
				gf.P("        public ", property.typ, " ", property.name, " { get; set; }")
			}
		}
		gf.P("    }")
		gf.P()
	}
}

func (g *Generator) packageMessages(pkg *contractmodel.Package) []*contractmodel.Message {
	messages := slices.Clone(pkg.Messages)
	if g.needsSyntheticEmpty(pkg) {
		messages = append(messages, &contractmodel.Message{Name: emptyTypeName})
	}
	return messages
}

func (g *Generator) needsSyntheticEmpty(pkg *contractmodel.Package) bool {
	if hasMessageNamed(pkg.Messages, emptyTypeName) {
		return false
	}
	for _, service := range pkg.Services {
		for _, method := range service.Methods {
			if method.InputType == emptyTypeName || method.ResponseType == emptyTypeName {
				return true
			}
		}
	}
	return false
}

func hasMessageNamed(messages []*contractmodel.Message, name string) bool {
	for _, message := range messages {
		if message.Name == name {
			return true
		}
	}
	return false
}

