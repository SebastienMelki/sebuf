func (g *Generator) appendStandardMessageProperties(
	properties *[]generatedProperty,
	usedJSONNames map[string]bool,
	skippedFields map[string]bool,
	message *contractmodel.Message,
	messageIndex map[string]*contractmodel.Message,
) {
	for _, field := range message.Fields {
		if skippedFields[field.Name] {
			continue
		}
		if field.Annotations.Flatten && g.appendFlattenedProperties(properties, usedJSONNames, field, messageIndex) {
			continue
		}
		g.appendProperty(
			properties,
			usedJSONNames,
			field.Name,
			pascalCase(field.Name),
			g.csharpPropertyType(field, false),
			g.enumConverterAttribute(field),
		)
	}
}

func (g *Generator) appendOneofProperties(
	properties *[]generatedProperty,
	usedJSONNames map[string]bool,
	message *contractmodel.Message,
	messageIndex map[string]*contractmodel.Message,
) {
	for _, oneof := range message.Oneofs {
		if oneof.Discriminator != "" {
			g.appendProperty(
				properties,
				usedJSONNames,
				oneof.Discriminator,
				pascalCase(oneof.Discriminator),
				"string?",
				"",
			)
		}
		for _, variant := range oneof.Variants {
			if oneof.Flatten && variant.IsMessage &&
				g.appendFlattenedVariantProperties(properties, usedJSONNames, variant, messageIndex) {
				continue
			}

			variantField := findField(message, variant.FieldName)
			if variantField == nil {
				continue
			}
			g.appendProperty(
				properties,
				usedJSONNames,
				variant.FieldName,
				pascalCase(variant.FieldName),
				g.csharpPropertyType(variantField, true),
				g.enumConverterAttribute(variantField),
			)
		}
	}
}

func (g *Generator) appendFlattenedProperties(
	properties *[]generatedProperty,
	usedJSONNames map[string]bool,
	field *contractmodel.Field,
	messageIndex map[string]*contractmodel.Message,
) bool {
	child := childMessage(messageIndex, field)
	if child == nil {
		return false
	}

	for _, childField := range child.Fields {
		jsonName := field.Annotations.FlattenPrefix + childField.Name
		g.appendProperty(
			properties,
			usedJSONNames,
			jsonName,
			pascalCase(jsonName),
			g.csharpPropertyType(childField, true),
			g.enumConverterAttribute(childField),
		)
	}
	return true
}

func (g *Generator) appendFlattenedVariantProperties(
	properties *[]generatedProperty,
	usedJSONNames map[string]bool,
	variant *contractmodel.OneofVariant,
	messageIndex map[string]*contractmodel.Message,
) bool {
	child := messageForRef(messageIndex, variant.Type)
	if child == nil {
		return false
	}

	for _, childField := range child.Fields {
		jsonName := childField.Name
		if usedJSONNames[jsonName] {
			return false
		}
	}

	for _, childField := range child.Fields {
		jsonName := childField.Name
		g.appendProperty(
			properties,
			usedJSONNames,
			jsonName,
			pascalCase(jsonName),
			g.csharpPropertyType(childField, true),
			g.enumConverterAttribute(childField),
		)
	}
	return true
}

func (g *Generator) appendProperty(
	properties *[]generatedProperty,
	usedJSONNames map[string]bool,
	jsonName string,
	name string,
	typ string,
	converter string,
) {
	if usedJSONNames[jsonName] {
		return
	}
	usedJSONNames[jsonName] = true
	*properties = append(*properties, generatedProperty{
		jsonName:    jsonName,
		name:        name,
		typ:         typ,
		converter:   converter,
		initializer: csharpPropertyInitializer(typ),
	})
}

// csharpPropertyInitializer preserves protobuf's non-null collection, bytes,
// string, and dynamic-object defaults while satisfying nullable reference type
// analysis for DTOs constructed before deserialization populates their fields.
func csharpPropertyInitializer(typ string) string {
	if strings.HasSuffix(typ, "?") {
		return ""
	}
	switch {
	case typ == csharpStringType:
		return "string.Empty"
	case typ == csharpObjectType:
		return "new object()"
	case strings.HasSuffix(typ, "[]"):
		return "Array.Empty<" + strings.TrimSuffix(typ, "[]") + ">()"
	case strings.HasPrefix(typ, "List<"), strings.HasPrefix(typ, "Dictionary<"):
		return "new()"
	default:
		return ""
	}
}

func childMessage(messageIndex map[string]*contractmodel.Message, field *contractmodel.Field) *contractmodel.Message {
	if field == nil || field.Type == nil || field.Type.Kind != contractmodel.KindMessage {
		return nil
	}
	return messageForRef(messageIndex, field.Type)
}

func findField(message *contractmodel.Message, name string) *contractmodel.Field {
	for _, field := range message.Fields {
		if field.Name == name {
			return field
		}
	}
	return nil
}

func isRootUnwrapMessage(message *contractmodel.Message) bool {
	return message != nil && message.Unwrap != nil && message.Unwrap.IsRoot && len(message.Fields) == 1
}

func (g *Generator) rootUnwrapBaseType(message *contractmodel.Message) string {
	if message == nil || len(message.Fields) == 0 {
		return csharpObjectType
	}
	field := message.Fields[0]
	if field.IsMap {
		return g.csharpBaseType(field)
	}
	if field.Repeated && field.Type != nil {
		return "List<" + g.csharpBaseType(&contractmodel.Field{Type: field.Type, Annotations: field.Annotations}) + ">"
	}
	return g.csharpPropertyType(field, false)
}

// rootUnwrapBaseType keeps the package-local helper API used by focused unit
// tests and by callers constructing contract models directly.
func rootUnwrapBaseType(message *contractmodel.Message) string {
	return (&Generator{}).rootUnwrapBaseType(message)
}

func (g *Generator) useNewtonsoft() bool {
	return strings.EqualFold(g.opts.JSONLib, "newtonsoft")
}

func (g *Generator) jsonAttribute(name string) string {
	if g.useNewtonsoft() {
		return `[JsonProperty("` + name + `")]`
	}
	return `[JsonPropertyName("` + name + `")]`
}

func csharpType(field *contractmodel.Field) string {
	return csharpPropertyType(field, false)
}

func (g *Generator) csharpPropertyType(field *contractmodel.Field, forceNullable bool) string {
	ref := field.Type
	if ref == nil {
		return csharpObjectType
	}
	base := g.csharpBaseType(field)
	if field.Repeated {
		if forceNullable {
			return "List<" + base + ">?"
		}
		return "List<" + base + ">"
	}
	if field.IsMap {
		if forceNullable {
			return base + "?"
		}
		return base
	}
	if shouldUseNullableType(field, ref, forceNullable) {
		return base + "?"
	}
	return base
}

func (g *Generator) csharpBaseType(field *contractmodel.Field) string {
	ref := field.Type
	if ref == nil {
		return csharpObjectType
	}

	switch ref.Kind {
	case contractmodel.KindMessage, contractmodel.KindEnum:
		return g.csharpNamedType(ref)
	case contractmodel.KindWellKnown:
		return csharpWellKnownType(field)
	case contractmodel.KindMap:
		return fmt.Sprintf(
			"Dictionary<%s, %s>", g.csharpBaseTypeForRef(ref.MapKey), g.csharpBaseTypeForRef(ref.MapValue),
		)
	case contractmodel.KindScalar:
		return csharpScalar(field)
	default:
		return csharpObjectType
	}
}

func (g *Generator) csharpBaseTypeForRef(ref *contractmodel.TypeRef) string {
	return g.csharpBaseType(&contractmodel.Field{Type: ref})
}

func (g *Generator) csharpNamedType(ref *contractmodel.TypeRef) string {
	if ref.Package == "" || ref.Package == g.currentPackage {
		return ref.Name
	}
	return "global::" + g.packageNamespace(ref.Package) + "." + ref.Name
}

func csharpPropertyType(field *contractmodel.Field, forceNullable bool) string {
	ref := field.Type
	if ref == nil {
		return csharpObjectType
	}
	base := csharpBaseType(field)
	if field.Repeated {
		if forceNullable {
			return "List<" + base + ">?"
		}
		return "List<" + base + ">"
	}
	if field.IsMap {
		if forceNullable {
			return base + "?"
		}
		return base
	}
	if shouldUseNullableType(field, ref, forceNullable) {
		return base + "?"
	}
	return base
}

func csharpBaseType(field *contractmodel.Field) string {
	ref := field.Type
	if ref == nil {
		return csharpObjectType
	}

	switch ref.Kind {
	case contractmodel.KindMessage:
		return ref.Name
	case contractmodel.KindEnum:
		return ref.Name
	case contractmodel.KindWellKnown:
		return csharpWellKnownType(field)
	case contractmodel.KindMap:
		return fmt.Sprintf("Dictionary<%s, %s>", csharpBaseTypeForRef(ref.MapKey), csharpBaseTypeForRef(ref.MapValue))
	case contractmodel.KindScalar:
		return csharpScalar(field)
	default:
		return csharpObjectType
	}
}

func csharpBaseTypeForRef(ref *contractmodel.TypeRef) string {
	return csharpBaseType(&contractmodel.Field{Type: ref})
}

func csharpWellKnownType(field *contractmodel.Field) string {
	ref := field.Type
	switch ref.WellKnown {
	case contractmodel.WellKnownStruct:
		return "Dictionary<string, object>"
	case contractmodel.WellKnownTimestamp:
		switch field.Annotations.TimestampFormat {
		case sebufhttp.TimestampFormat_TIMESTAMP_FORMAT_UNIX_SECONDS,
			sebufhttp.TimestampFormat_TIMESTAMP_FORMAT_UNIX_MILLIS:
			return csharpLongType
		case sebufhttp.TimestampFormat_TIMESTAMP_FORMAT_UNSPECIFIED,
			sebufhttp.TimestampFormat_TIMESTAMP_FORMAT_RFC3339,
			sebufhttp.TimestampFormat_TIMESTAMP_FORMAT_DATE:
			return csharpStringType
		default:
			return csharpStringType
		}
	case contractmodel.WellKnownDuration, contractmodel.WellKnownFieldMask:
		return csharpStringType
	case contractmodel.WellKnownListValue:
		return "List<object>"
	case contractmodel.WellKnownAny, contractmodel.WellKnownValue:
		return csharpObjectType
	case contractmodel.WellKnownEmpty:
		return "object"
	case contractmodel.WellKnownDoubleWrap,
		contractmodel.WellKnownFloatWrap,
		contractmodel.WellKnownInt64Wrap,
		contractmodel.WellKnownUInt64Wrap,
		contractmodel.WellKnownInt32Wrap,
		contractmodel.WellKnownUInt32Wrap,
		contractmodel.WellKnownBoolWrap,
		contractmodel.WellKnownStringWrap,
		contractmodel.WellKnownBytesWrap:
		return csharpScalar(&contractmodel.Field{Type: ref, Annotations: field.Annotations})
	default:
		return csharpObjectType
	}
}

func csharpScalar(field *contractmodel.Field) string {
	kind := field.Type.Name
	switch kind {
	case csharpDoubleType:
		return csharpDoubleType
	case "float":
		return "float"
	case "int64", "sfixed64", "sint64":
		if field.Annotations.Int64Encoding != sebufhttp.Int64Encoding_INT64_ENCODING_NUMBER {
			return csharpStringType
		}
		return csharpLongType
	case "uint64", "fixed64":
		if field.Annotations.Int64Encoding != sebufhttp.Int64Encoding_INT64_ENCODING_NUMBER {
			return csharpStringType
		}
		return "ulong"
	case "int32", "sfixed32", "sint32":
		return "int"
	case "uint32", "fixed32":
		return "uint"
	case csharpBoolType:
		return csharpBoolType
	case csharpStringType:
		return csharpStringType
	case bytesTypeName:
		return "byte[]"
	default:
		return csharpObjectType
	}
}

func (g *Generator) enumConverterAttribute(field *contractmodel.Field) string {
	// Enum wire encoding is normalized at the message boundary. Property-level
	// converters cannot correctly cover repeated or map enum values and
	// System.Text.Json's built-in converter does not honor EnumMember mappings.
	return ""
}

func shouldUseNullableType(field *contractmodel.Field, ref *contractmodel.TypeRef, forceNullable bool) bool {
	if ref == nil {
		return false
	}
	if forceNullable {
		return true
	}
	if ref.Kind == contractmodel.KindWellKnown && isWrapper(ref.WellKnown) {
		return true
	}
	if field.Annotations.Nullable || field.Annotations.EmptyBehavior == sebufhttp.EmptyBehavior_EMPTY_BEHAVIOR_NULL ||
		field.Annotations.EmptyBehavior == sebufhttp.EmptyBehavior_EMPTY_BEHAVIOR_OMIT {
		return true
	}
	if !field.Optional && !field.HasPresence && !field.IsOneofVariant {
		return false
	}
	return true
}

func isWrapper(kind contractmodel.WellKnownType) bool {
	switch kind {
	case contractmodel.WellKnownDoubleWrap,
		contractmodel.WellKnownFloatWrap,
		contractmodel.WellKnownInt64Wrap,
		contractmodel.WellKnownUInt64Wrap,
		contractmodel.WellKnownInt32Wrap,
		contractmodel.WellKnownUInt32Wrap,
		contractmodel.WellKnownBoolWrap,
		contractmodel.WellKnownStringWrap,
		contractmodel.WellKnownBytesWrap:
		return true
	case contractmodel.WellKnownAny,
		contractmodel.WellKnownDuration,
		contractmodel.WellKnownEmpty,
		contractmodel.WellKnownFieldMask,
		contractmodel.WellKnownListValue,
		contractmodel.WellKnownStruct,
		contractmodel.WellKnownTimestamp,
		contractmodel.WellKnownValue:
		return false
	default:
		return false
	}
}

func pascalCase(name string) string {
	parts := strings.Split(name, "_")
	for i, part := range parts {
		if part == "" {
			continue
		}
		part = strings.ToLower(part)
		parts[i] = strings.ToUpper(part[:1]) + part[1:]
	}
	return strings.Join(parts, "")
}

func upperFirst(value string) string {
	if value == "" {
		return ""
	}
	return strings.ToUpper(value[:1]) + value[1:]
}
