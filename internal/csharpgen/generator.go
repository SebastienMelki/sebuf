package csharpgen

import (
	"flag"
	"fmt"
	"path"
	"strings"

	"google.golang.org/protobuf/compiler/protogen"

	sebufhttp "github.com/SebastienMelki/sebuf/http"
	"github.com/SebastienMelki/sebuf/internal/contractmodel"
)

const (
	csharpObjectType = "object"
	csharpStringType = "string"
	csharpDoubleType = "double"
	csharpBoolType   = "bool"
	csharpLongType   = "long"
)

type Options struct {
	Namespace string
	JSONLib   string
}

type Generator struct {
	plugin *protogen.Plugin
	opts   Options
}

func New(plugin *protogen.Plugin, opts Options) *Generator {
	return &Generator{plugin: plugin, opts: opts}
}

func NewOptions() (protogen.Options, *Options) {
	var flags flag.FlagSet
	cfg := &Options{}
	flags.StringVar(&cfg.Namespace, "namespace", "Sebuf.Generated", "C# namespace")
	flags.StringVar(&cfg.JSONLib, "json_lib", "newtonsoft", "JSON annotation library: newtonsoft or system_text_json")
	return protogen.Options{ParamFunc: flags.Set}, cfg
}

func (g *Generator) Generate() error {
	for _, pkg := range contractmodel.Packages(g.plugin.Files) {
		if err := g.generatePackage(pkg); err != nil {
			return err
		}
	}
	return nil
}

func (g *Generator) generatePackage(pkg *contractmodel.Package) error {
	packagePath := strings.ReplaceAll(pkg.Name, ".", "/")
	gf := g.plugin.NewGeneratedFile(path.Join(packagePath, "Contracts.g.cs"), "")
	gf.P("// Code generated by protoc-gen-csharp-http. DO NOT EDIT.")
	gf.P("using System.Collections.Generic;")
	gf.P("using System.Runtime.Serialization;")
	if g.useNewtonsoft() {
		gf.P("using Newtonsoft.Json;")
		gf.P("using Newtonsoft.Json.Converters;")
	} else {
		gf.P("using System.Text.Json.Serialization;")
	}
	gf.P()
	gf.P("namespace ", g.opts.Namespace)
	gf.P("{")

	for _, enum := range pkg.Enums {
		gf.P("    public enum ", enum.Name)
		gf.P("    {")
		for _, value := range enum.Values {
			gf.P(`        [EnumMember(Value = "`, value.JSONValue, `")]`)
			gf.P("        ", pascalCase(value.Name), " = ", value.Number, ",")
		}
		gf.P("    }")
		gf.P()
	}

	for _, message := range pkg.Messages {
		gf.P("    public sealed class ", message.Name)
		gf.P("    {")
		for _, field := range message.Fields {
			gf.P("        ", g.jsonAttribute(field.Name))
			if converter := g.enumConverterAttribute(field); converter != "" {
				gf.P("        ", converter)
			}
			gf.P(
				"        public ",
				csharpType(field),
				" ",
				pascalCase(field.Name),
				" { get; set; }",
			)
		}
		gf.P("    }")
		gf.P()
	}

	gf.P("    public static class ServiceContracts")
	gf.P("    {")
	for _, service := range pkg.Services {
		gf.P("        public static class ", service.Name)
		gf.P("        {")
		gf.P(`            public const string Name = "`, service.Name, `";`)
		gf.P(`            public const string BasePath = "`, service.BasePath, `";`)
		for _, method := range service.Methods {
			gf.P("            public static class ", method.Name)
			gf.P("            {")
			gf.P(`                public const string HttpMethod = "`, method.HTTPMethod, `";`)
			gf.P(`                public const string Path = "`, method.Path, `";`)
			gf.P(`                public const string RequestType = "`, method.InputType, `";`)
			gf.P(`                public const string ResponseType = "`, method.ResponseType, `";`)
			gf.P("            }")
		}
		gf.P("        }")
	}
	gf.P("    }")
	gf.P("}")
	return nil
}

func (g *Generator) useNewtonsoft() bool {
	return strings.EqualFold(g.opts.JSONLib, "newtonsoft")
}

func (g *Generator) jsonAttribute(name string) string {
	if g.useNewtonsoft() {
		return `[JsonProperty("` + name + `")]`
	}
	return `[JsonPropertyName("` + name + `")]`
}

func csharpType(field *contractmodel.Field) string {
	ref := field.Type
	if ref == nil {
		return csharpObjectType
	}
	base := csharpBaseType(field)
	if field.Repeated {
		return "List<" + base + ">"
	}
	if shouldUseNullableType(field, ref, base) {
		return base + "?"
	}
	return base
}

func csharpBaseType(field *contractmodel.Field) string {
	ref := field.Type
	if ref == nil {
		return csharpObjectType
	}

	switch ref.Kind {
	case contractmodel.KindMessage:
		return ref.Name
	case contractmodel.KindEnum:
		return ref.Name
	case contractmodel.KindWellKnown:
		return csharpWellKnownType(field)
	case contractmodel.KindMap:
		return fmt.Sprintf("Dictionary<%s, %s>", csharpBaseTypeForRef(ref.MapKey), csharpBaseTypeForRef(ref.MapValue))
	case contractmodel.KindScalar:
		return csharpScalar(field)
	default:
		return csharpObjectType
	}
}

func csharpBaseTypeForRef(ref *contractmodel.TypeRef) string {
	return csharpBaseType(&contractmodel.Field{Type: ref})
}

func csharpWellKnownType(field *contractmodel.Field) string {
	ref := field.Type
	switch ref.WellKnown {
	case contractmodel.WellKnownStruct:
		return "Dictionary<string, object>"
	case contractmodel.WellKnownTimestamp:
		switch field.Annotations.TimestampFormat {
		case sebufhttp.TimestampFormat_TIMESTAMP_FORMAT_UNIX_SECONDS,
			sebufhttp.TimestampFormat_TIMESTAMP_FORMAT_UNIX_MILLIS:
			return csharpLongType
		case sebufhttp.TimestampFormat_TIMESTAMP_FORMAT_UNSPECIFIED,
			sebufhttp.TimestampFormat_TIMESTAMP_FORMAT_RFC3339,
			sebufhttp.TimestampFormat_TIMESTAMP_FORMAT_DATE:
			return csharpStringType
		default:
			return csharpStringType
		}
	case contractmodel.WellKnownDuration, contractmodel.WellKnownFieldMask:
		return csharpStringType
	case contractmodel.WellKnownListValue:
		return "List<object>"
	case contractmodel.WellKnownAny, contractmodel.WellKnownValue:
		return csharpObjectType
	case contractmodel.WellKnownEmpty:
		return "object"
	case contractmodel.WellKnownDoubleWrap,
		contractmodel.WellKnownFloatWrap,
		contractmodel.WellKnownInt64Wrap,
		contractmodel.WellKnownUInt64Wrap,
		contractmodel.WellKnownInt32Wrap,
		contractmodel.WellKnownUInt32Wrap,
		contractmodel.WellKnownBoolWrap,
		contractmodel.WellKnownStringWrap,
		contractmodel.WellKnownBytesWrap:
		return csharpScalar(&contractmodel.Field{Type: ref, Annotations: field.Annotations})
	default:
		return csharpObjectType
	}
}

func csharpScalar(field *contractmodel.Field) string {
	kind := field.Type.Name
	switch kind {
	case csharpDoubleType, "float":
		return csharpDoubleType
	case "int64", "uint64", "fixed64", "sfixed64", "sint64":
		if field.Annotations.Int64Encoding != sebufhttp.Int64Encoding_INT64_ENCODING_NUMBER {
			return csharpStringType
		}
		return csharpLongType
	case "int32", "fixed32", "uint32", "sfixed32", "sint32":
		return "int"
	case csharpBoolType:
		return csharpBoolType
	case csharpStringType, "bytes":
		return csharpStringType
	default:
		return csharpObjectType
	}
}

func (g *Generator) enumConverterAttribute(field *contractmodel.Field) string {
	if field.Type == nil || field.Type.Kind != contractmodel.KindEnum {
		return ""
	}
	if field.Annotations.EnumEncoding == sebufhttp.EnumEncoding_ENUM_ENCODING_NUMBER {
		return ""
	}
	if g.useNewtonsoft() {
		return `[JsonConverter(typeof(StringEnumConverter))]`
	}
	return `[JsonConverter(typeof(JsonStringEnumConverter))]`
}

func shouldUseNullableType(field *contractmodel.Field, ref *contractmodel.TypeRef, base string) bool {
	if ref == nil {
		return false
	}
	if ref.Kind == contractmodel.KindWellKnown && isWrapper(ref.WellKnown) {
		return isCSharpValueType(base)
	}
	if !field.Optional && (!field.HasPresence || ref.Kind != contractmodel.KindEnum) {
		return false
	}
	return isCSharpValueType(base)
}

func isWrapper(kind contractmodel.WellKnownType) bool {
	switch kind {
	case contractmodel.WellKnownDoubleWrap,
		contractmodel.WellKnownFloatWrap,
		contractmodel.WellKnownInt64Wrap,
		contractmodel.WellKnownUInt64Wrap,
		contractmodel.WellKnownInt32Wrap,
		contractmodel.WellKnownUInt32Wrap,
		contractmodel.WellKnownBoolWrap,
		contractmodel.WellKnownStringWrap,
		contractmodel.WellKnownBytesWrap:
		return true
	case contractmodel.WellKnownAny,
		contractmodel.WellKnownDuration,
		contractmodel.WellKnownEmpty,
		contractmodel.WellKnownFieldMask,
		contractmodel.WellKnownListValue,
		contractmodel.WellKnownStruct,
		contractmodel.WellKnownTimestamp,
		contractmodel.WellKnownValue:
		return false
	default:
		return false
	}
}

func isCSharpValueType(name string) bool {
	switch name {
	case csharpDoubleType, csharpLongType, "int", csharpBoolType:
		return true
	default:
		return !strings.Contains(name, "string") && !strings.Contains(name, "object") &&
			!strings.Contains(name, "List<") && !strings.Contains(name, "Dictionary<")
	}
}

func pascalCase(name string) string {
	parts := strings.Split(name, "_")
	for i, part := range parts {
		if part == "" {
			continue
		}
		part = strings.ToLower(part)
		parts[i] = strings.ToUpper(part[:1]) + part[1:]
	}
	return strings.Join(parts, "")
}
