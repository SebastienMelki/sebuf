func csharpQueryCondition(field *contractmodel.Field, expr string) string {
	if field == nil || field.Type == nil {
		return expr + " != null"
	}
	if field.Optional || field.HasPresence {
		return expr + " != null"
	}
	switch field.Type.Kind {
	case contractmodel.KindScalar:
		switch field.Type.Name {
		case csharpStringType, bytesTypeName:
			return "!string.IsNullOrEmpty(" + expr + ")"
		case "bool":
			return expr
		case "int32", "fixed32", "uint32", "sfixed32", "sint32", "int64", "uint64", "fixed64", "sfixed64", "sint64":
			return expr + " != 0"
		case "float", "double":
			return expr + " != 0"
		default:
			return expr + " != null"
		}
	case contractmodel.KindEnum:
		return expr + " != 0"
	case contractmodel.KindMessage, contractmodel.KindWellKnown, contractmodel.KindMap:
		return expr + " != null"
	default:
		return expr + " != null"
	}
}

