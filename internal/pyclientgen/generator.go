package pyclientgen

import (
	"flag"
	"fmt"
	"path"
	"sort"
	"strings"

	"google.golang.org/protobuf/compiler/protogen"

	sebufhttp "github.com/SebastienMelki/sebuf/http"
	"github.com/SebastienMelki/sebuf/internal/contractmodel"
)

const (
	pythonAnyType    = "Any"
	pythonBytesType  = "bytes"
	pythonFloatType  = "float"
	pythonIntType    = "int"
	pythonBoolType   = "bool"
	pythonStringType = "str"
	emptyTypeName    = "Empty"
)

type Options struct {
	Package string
}

type Generator struct {
	plugin *protogen.Plugin
	opts   Options
}

func New(plugin *protogen.Plugin, opts Options) *Generator {
	return &Generator{plugin: plugin, opts: opts}
}

func NewOptions() (protogen.Options, *Options) {
	var flags flag.FlagSet
	cfg := &Options{}
	flags.StringVar(&cfg.Package, "package", "", "Override Python package output path")
	return protogen.Options{ParamFunc: flags.Set}, cfg
}

func (g *Generator) Generate() error {
	for _, pkg := range contractmodel.Packages(g.plugin.Files) {
		if err := g.generatePackage(pkg); err != nil {
			return err
		}
	}
	return nil
}

func (g *Generator) generatePackage(pkg *contractmodel.Package) error {
	packagePath := g.outputPackagePath(pkg.Name)
	for _, initPath := range initFiles(packagePath) {
		gf := g.plugin.NewGeneratedFile(initPath, "")
		gf.P("")
	}

	gf := g.plugin.NewGeneratedFile(path.Join(packagePath, "contracts.py"), "")
	messages := packageMessages(pkg)
	messageIndex := make(map[string]*contractmodel.Message, len(messages))
	for _, message := range messages {
		messageIndex[message.Name] = message
	}

	gf.P(`"""Code generated by protoc-gen-py-client. DO NOT EDIT."""`)
	gf.P("from __future__ import annotations")
	gf.P()
	gf.P("import base64")
	gf.P("import json")
	gf.P("from dataclasses import dataclass, field")
	gf.P("from enum import IntEnum")
	gf.P("from typing import Any, ClassVar, Mapping")
	gf.P("from urllib.error import HTTPError")
	gf.P("from urllib.parse import quote, urlencode")
	gf.P("from urllib.request import Request, urlopen")
	gf.P()

	g.generateSharedTypes(gf)
	for _, enum := range pkg.Enums {
		g.generateEnum(gf, enum)
	}
	for _, message := range messages {
		g.generateMessage(gf, message, messageIndex)
	}
	g.generateServiceDescriptors(gf, pkg.Services)
	for _, service := range pkg.Services {
		g.generateClient(gf, service)
	}

	return nil
}

func (g *Generator) outputPackagePath(protoPackage string) string {
	if g.opts.Package != "" {
		return strings.Trim(strings.ReplaceAll(g.opts.Package, ".", "/"), "/")
	}
	return strings.ReplaceAll(protoPackage, ".", "/")
}

func (g *Generator) generateSharedTypes(gf *protogen.GeneratedFile) {
	gf.P("@dataclass(frozen=True, slots=True)")
	gf.P("class MethodDescriptor:")
	gf.P("    name: str")
	gf.P("    http_method: str")
	gf.P("    path: str")
	gf.P("    request_type: type[Any]")
	gf.P("    response_type: type[Any]")
	gf.P("    path_params: tuple[str, ...] = ()")
	gf.P()
	gf.P("@dataclass(frozen=True, slots=True)")
	gf.P("class ServiceDescriptor:")
	gf.P("    name: str")
	gf.P("    base_path: str")
	gf.P("    methods: dict[str, MethodDescriptor]")
	gf.P()
	gf.P("class ApiException(RuntimeError):")
	gf.P("    def __init__(self, status_code: int, body: str):")
	gf.P(`        super().__init__(f"HTTP {status_code}: {body}")`)
	gf.P("        self.status_code = status_code")
	gf.P("        self.body = body")
	gf.P()
	gf.P("def _encode_bytes(value: bytes, encoding: str) -> str:")
	gf.P(`    if encoding in ("base64", "base64_raw"):`)
	gf.P("        encoded = base64.b64encode(value).decode('ascii')")
	gf.P(`        return encoded.rstrip("=") if encoding == "base64_raw" else encoded`)
	gf.P(`    if encoding in ("base64url", "base64url_raw"):`)
	gf.P("        encoded = base64.urlsafe_b64encode(value).decode('ascii')")
	gf.P(`        return encoded.rstrip("=") if encoding == "base64url_raw" else encoded`)
	gf.P(`    if encoding == "hex":`)
	gf.P("        return value.hex()")
	gf.P(`    raise ValueError(f"unsupported bytes encoding: {encoding}")`)
	gf.P()
	gf.P("def _decode_bytes(value: str, encoding: str) -> bytes:")
	gf.P(`    if encoding == "base64":`)
	gf.P("        return base64.b64decode(value.encode('ascii'))")
	gf.P(`    if encoding == "base64_raw":`)
	gf.P(`        padded = value + "=" * (-len(value) % 4)`)
	gf.P("        return base64.b64decode(padded.encode('ascii'))")
	gf.P(`    if encoding == "base64url":`)
	gf.P("        return base64.urlsafe_b64decode(value.encode('ascii'))")
	gf.P(`    if encoding == "base64url_raw":`)
	gf.P(`        padded = value + "=" * (-len(value) % 4)`)
	gf.P("        return base64.urlsafe_b64decode(padded.encode('ascii'))")
	gf.P(`    if encoding == "hex":`)
	gf.P("        return bytes.fromhex(value)")
	gf.P(`    raise ValueError(f"unsupported bytes encoding: {encoding}")`)
	gf.P()
}

func (g *Generator) generateEnum(gf *protogen.GeneratedFile, enum *contractmodel.Enum) {
	gf.P("class ", enum.Name, "(IntEnum):")
	if len(enum.Values) == 0 {
		gf.P("    pass")
	} else {
		for _, value := range enum.Values {
			gf.P("    ", value.Name, " = ", value.Number)
		}
	}
	gf.P()
	gf.P("_", enum.Name, "_TO_JSON: dict[", enum.Name, ", str] = {")
	for _, value := range enum.Values {
		gf.P("    ", enum.Name, ".", value.Name, `: "`, value.JSONValue, `",`)
	}
	gf.P("}")
	gf.P("_", enum.Name, "_FROM_JSON: dict[str, ", enum.Name, "] = {")
	for _, value := range enum.Values {
		gf.P(`    "`, value.JSONValue, `": `, enum.Name, ".", value.Name, ",")
	}
	gf.P("}")
	gf.P()
}

func (g *Generator) generateMessage(
	gf *protogen.GeneratedFile,
	message *contractmodel.Message,
	messageIndex map[string]*contractmodel.Message,
) {
	fields := effectiveFields(message, messageIndex)
	gf.P("@dataclass(slots=True)")
	gf.P("class ", message.Name, ":")
	if len(fields) == 0 {
		gf.P("    def to_dict(self) -> Any:")
		gf.P("        return {}")
		gf.P()
		gf.P("    @classmethod")
		gf.P("    def from_dict(cls, data: Mapping[str, Any] | None) -> ", message.Name, ":")
		gf.P("        _ = data")
		gf.P("        return cls()")
		gf.P()
		gf.P("    def is_empty(self) -> bool:")
		gf.P("        return True")
		gf.P()
		return
	}

	for _, field := range fields {
		gf.P("    ", field.Name, ": ", pythonFieldType(field), pythonFieldDefault(field))
	}
	gf.P()
	g.generateToDict(gf, message, fields)
	g.generateFromDict(gf, message, fields)
	gf.P("    def is_empty(self) -> bool:")
	gf.P("        return self.to_dict() in ({}, [], None)")
	gf.P()
}

func (g *Generator) generateToDict(
	gf *protogen.GeneratedFile,
	message *contractmodel.Message,
	fields []*contractmodel.Field,
) {
	gf.P("    def to_dict(self) -> Any:")
	if isRootUnwrapMessage(message) {
		field := message.Fields[0]
		gf.P("        value = self.", field.Name)
		gf.P("        return ", serializeExpr(field))
		gf.P()
		return
	}

	gf.P("        result: dict[str, Any] = {}")
	for _, field := range fields {
		gf.P("        value = self.", field.Name)
		if shouldSkipNone(field) {
			gf.P("        if value is not None:")
			if usesEmptyBehavior(field) {
				g.generateEmptyBehavior(gf, field, field.JSONName, "            ")
			} else {
				gf.P(`            result["`, field.JSONName, `"] = `, serializeExpr(field))
			}
			continue
		}
		if usesEmptyBehavior(field) {
			g.generateEmptyBehavior(gf, field, field.JSONName, "        ")
			continue
		}
		gf.P(`        result["`, field.JSONName, `"] = `, serializeExpr(field))
	}
	g.generateOneofSerialize(gf, message)
	gf.P("        return result")
	gf.P()
}

func (g *Generator) generateFromDict(
	gf *protogen.GeneratedFile,
	message *contractmodel.Message,
	fields []*contractmodel.Field,
) {
	gf.P("    @classmethod")
	gf.P("    def from_dict(cls, data: Mapping[str, Any] | None) -> ", message.Name, ":")
	gf.P("        if data is None:")
	gf.P("            return cls()")
	if isRootUnwrapMessage(message) {
		field := message.Fields[0]
		gf.P("        return cls(", field.Name, "=", deserializeExpr(field, "data"), ")")
		gf.P()
		return
	}

	gf.P("        kwargs: dict[str, Any] = {}")
	for _, field := range fields {
		gf.P(`        if "`, field.JSONName, `" in data:`)
		gf.P(`            raw_`, field.Name, ` = data["`, field.JSONName, `"]`)
		if shouldSkipNone(field) {
			gf.P(`            if raw_`, field.Name, ` is None:`)
			gf.P(`                kwargs["`, field.Name, `"] = None`)
			gf.P("            else:")
			gf.P(`                kwargs["`, field.Name, `"] = `, deserializeExpr(field, "raw_"+field.Name))
		} else {
			gf.P(`            kwargs["`, field.Name, `"] = `, deserializeExpr(field, "raw_"+field.Name))
		}
	}
	g.generateOneofDeserialize(gf, message)
	gf.P("        return cls(**kwargs)")
	gf.P()
}

func (g *Generator) generateEmptyBehavior(
	gf *protogen.GeneratedFile,
	field *contractmodel.Field,
	jsonName, indent string,
) {
	gf.P(indent, "if hasattr(value, 'is_empty') and value.is_empty():")
	switch field.Annotations.EmptyBehavior {
	case sebufhttp.EmptyBehavior_EMPTY_BEHAVIOR_NULL:
		gf.P(indent, `    result["`, jsonName, `"] = None`)
	case sebufhttp.EmptyBehavior_EMPTY_BEHAVIOR_OMIT:
		gf.P(indent, "    pass")
	case sebufhttp.EmptyBehavior_EMPTY_BEHAVIOR_UNSPECIFIED,
		sebufhttp.EmptyBehavior_EMPTY_BEHAVIOR_PRESERVE:
		gf.P(indent, `    result["`, jsonName, `"] = `, serializeExpr(field))
	default:
		gf.P(indent, `    result["`, jsonName, `"] = `, serializeExpr(field))
	}
	gf.P(indent, "else:")
	gf.P(indent, `    result["`, jsonName, `"] = `, serializeExpr(field))
}

func (g *Generator) generateOneofSerialize(gf *protogen.GeneratedFile, message *contractmodel.Message) {
	for _, oneof := range message.Oneofs {
		if oneof.Discriminator == "" || oneof.Flatten {
			continue
		}
		for _, variant := range oneof.Variants {
			gf.P("        if self.", variant.FieldName, " is not None:")
			gf.P(`            result["`, oneof.Discriminator, `"] = "`, variant.DiscriminatorValue, `"`)
			if oneof.Flatten && variant.IsMessage {
				gf.P("            result.update(self.", variant.FieldName, ".to_dict())")
			}
		}
	}
}

func (g *Generator) generateOneofDeserialize(gf *protogen.GeneratedFile, message *contractmodel.Message) {
	for _, oneof := range message.Oneofs {
		if oneof.Discriminator == "" || oneof.Flatten {
			continue
		}
		gf.P(`        discriminator_`, oneof.Name, ` = data.get("`, oneof.Discriminator, `")`)
		for _, variant := range oneof.Variants {
			gf.P(`        if discriminator_`, oneof.Name, ` == "`, variant.DiscriminatorValue, `":`)
			if oneof.Flatten && variant.IsMessage {
				gf.P(`            kwargs["`, variant.FieldName, `"] = `, variant.Type.Name, ".from_dict(data)")
			} else {
				gf.P(`            if "`, variant.FieldName, `" in data:`)
				expr := deserializeBaseExpr(&contractmodel.Field{Type: variant.Type}, `data["`+variant.FieldName+`"]`)
				gf.P(`                kwargs["`, variant.FieldName, `"] = `, expr)
			}
		}
	}
}

func (g *Generator) generateServiceDescriptors(gf *protogen.GeneratedFile, services []*contractmodel.Service) {
	gf.P("SERVICES: dict[str, ServiceDescriptor] = {")
	for _, service := range services {
		gf.P(`    "`, service.Name, `": ServiceDescriptor(`)
		gf.P(`        name="`, service.Name, `",`)
		gf.P(`        base_path="`, service.BasePath, `",`)
		gf.P("        methods={")
		for _, method := range service.Methods {
			gf.P(`            "`, method.Name, `": MethodDescriptor(`)
			gf.P(`                name="`, method.Name, `",`)
			gf.P(`                http_method="`, method.HTTPMethod, `",`)
			gf.P(`                path="`, method.Path, `",`)
			gf.P("                request_type=", method.InputType, ",")
			gf.P("                response_type=", method.ResponseType, ",")
			gf.P("                path_params=(", joinQuoted(method.PathParams), "),")
			gf.P("            ),")
		}
		gf.P("        },")
		gf.P("    ),")
	}
	gf.P("}")
	gf.P()
}

func (g *Generator) generateClient(gf *protogen.GeneratedFile, service *contractmodel.Service) {
	clientName := service.Name + "Client"
	gf.P("class ", clientName, ":")
	gf.P(`    SERVICE: ClassVar[ServiceDescriptor] = SERVICES["`, service.Name, `"]`)
	gf.P()
	gf.P("    def __init__(")
	gf.P("        self,")
	gf.P("        base_url: str,")
	gf.P("        headers: Mapping[str, str] | None = None,")
	gf.P("        timeout: float | None = None,")
	gf.P("    ):")
	gf.P("        self._base_url = base_url.rstrip('/')")
	gf.P("        self._headers = dict(headers or {})")
	gf.P("        self._timeout = timeout")
	gf.P()
	for _, method := range service.Methods {
		g.generateClientMethod(gf, service, method)
	}
	gf.P("    def _request(self, method: str, path: str, query: dict[str, Any], body: Any) -> Any:")
	gf.P("        url = self._base_url + path")
	gf.P("        query = {key: value for key, value in query.items() if value is not None}")
	gf.P("        if query:")
	gf.P("            url += '?' + urlencode(query, doseq=True)")
	gf.P("        headers = dict(self._headers)")
	gf.P("        data: bytes | None = None")
	gf.P(`        headers.setdefault("Accept", "application/json")`)
	gf.P("        if body is not None:")
	gf.P(`            headers.setdefault("Content-Type", "application/json")`)
	gf.P("            data = json.dumps(body).encode('utf-8')")
	gf.P("        request = Request(url=url, data=data, headers=headers, method=method)")
	gf.P("        try:")
	gf.P("            with urlopen(request, timeout=self._timeout) as response:")
	gf.P("                payload = response.read()")
	gf.P("        except HTTPError as exc:")
	gf.P("            body_text = exc.read().decode('utf-8', errors='replace')")
	gf.P("            raise ApiException(exc.code, body_text) from exc")
	gf.P("        if not payload:")
	gf.P("            return None")
	gf.P("        return json.loads(payload.decode('utf-8'))")
	gf.P()
}

func (g *Generator) generateClientMethod(
	gf *protogen.GeneratedFile,
	_ *contractmodel.Service,
	method *contractmodel.Method,
) {
	gf.P("    def ", pythonMethodName(method.Name), "(")
	gf.P("        self,")
	gf.P("        request: ", method.InputType, ",")
	gf.P("    ) -> ", method.ResponseType, ":")
	gf.P("        descriptor = self.SERVICE.methods[", `"`, method.Name, `"`, "]")
	gf.P("        path = descriptor.path")
	for _, pathParam := range method.PathParams {
		gf.P(`        path = path.replace("{`, pathParam, `}", quote(str(request.`, pathParam, `), safe=""))`)
	}
	gf.P("        query: dict[str, Any] = {}")
	gf.P("        request_dict = request.to_dict()")
	for _, pathParam := range method.PathParams {
		gf.P(`        request_dict.pop("`, jsonNameForField(method.InputType, pathParam, g.plugin.Files), `", None)`)
	}
	for _, query := range queryFieldsForInput(method.InputType, g.plugin.Files) {
		gf.P(`        if "`, query.JSONName, `" in request_dict:`)
		gf.P(`            query["`, query.ParamName, `"] = request_dict.pop("`, query.JSONName, `")`)
	}
	if strings.EqualFold(method.HTTPMethod, "GET") || strings.EqualFold(method.HTTPMethod, "DELETE") {
		gf.P("        body = None")
	} else {
		gf.P("        body = request_dict")
	}
	gf.P("        payload = self._request(descriptor.http_method, path, query, body)")
	if method.ResponseType == emptyTypeName {
		gf.P("        return Empty()")
	} else {
		gf.P("        return ", method.ResponseType, ".from_dict(payload)")
	}
	gf.P()
}

type queryField struct {
	JSONName  string
	ParamName string
}

func queryFieldsForInput(inputType string, files []*protogen.File) []queryField {
	for _, pkg := range contractmodel.Packages(files) {
		for _, message := range pkg.Messages {
			if message.Name != inputType {
				continue
			}
			var result []queryField
			for _, field := range message.Fields {
				if field.Annotations.Query == nil {
					continue
				}
				result = append(result, queryField{JSONName: field.JSONName, ParamName: field.Annotations.Query.Name})
			}
			return result
		}
	}
	return nil
}

func jsonNameForField(inputType, fieldName string, files []*protogen.File) string {
	for _, pkg := range contractmodel.Packages(files) {
		for _, message := range pkg.Messages {
			if message.Name != inputType {
				continue
			}
			for _, field := range message.Fields {
				if field.Name == fieldName {
					return field.JSONName
				}
			}
		}
	}
	return fieldName
}

func initFiles(packagePath string) []string {
	if packagePath == "" {
		return nil
	}
	parts := strings.Split(packagePath, "/")
	files := make([]string, 0, len(parts))
	current := make([]string, 0, len(parts))
	for _, part := range parts {
		current = append(current, part)
		files = append(files, path.Join(path.Join(current...), "__init__.py"))
	}
	return files
}

func orderedFields(fields []*contractmodel.Field) []*contractmodel.Field {
	required := make([]*contractmodel.Field, 0, len(fields))
	optional := make([]*contractmodel.Field, 0, len(fields))
	for _, field := range fields {
		if hasDefault(field) {
			optional = append(optional, field)
			continue
		}
		required = append(required, field)
	}
	return append(required, optional...)
}

func effectiveFields(
	message *contractmodel.Message,
	messageIndex map[string]*contractmodel.Message,
) []*contractmodel.Field {
	if message == nil {
		return nil
	}

	var expanded []*contractmodel.Field
	skipFields := make(map[string]struct{})
	seen := make(map[string]struct{})

	addOneofExpandedFields(message, messageIndex, &expanded, seen, skipFields)

	for _, field := range message.Fields {
		if _, skipped := skipFields[field.Name]; skipped {
			continue
		}
		if field.Annotations.Flatten && field.Type != nil && field.Type.Kind == contractmodel.KindMessage {
			for _, flattened := range flattenChildFields(field.Annotations.FlattenPrefix, messageIndex[field.Type.Name], messageIndex) {
				addExpandedField(&expanded, seen, flattened)
			}
			continue
		}
		addExpandedField(&expanded, seen, cloneField(field))
	}

	return orderedFields(expanded)
}

func addOneofExpandedFields(
	message *contractmodel.Message,
	messageIndex map[string]*contractmodel.Message,
	expanded *[]*contractmodel.Field,
	seen map[string]struct{},
	skipFields map[string]struct{},
) {
	for _, oneof := range message.Oneofs {
		addOneofDiscriminator(oneof, expanded, seen)
		if !oneof.Flatten {
			continue
		}
		addFlattenedOneofVariantFields(oneof, messageIndex, expanded, seen, skipFields)
	}
}

func addOneofDiscriminator(
	oneof *contractmodel.Oneof,
	expanded *[]*contractmodel.Field,
	seen map[string]struct{},
) {
	if oneof == nil || oneof.Discriminator == "" {
		return
	}
	addExpandedField(expanded, seen, &contractmodel.Field{
		Name:        oneof.Discriminator,
		JSONName:    oneof.Discriminator,
		Optional:    true,
		HasPresence: true,
		Type:        &contractmodel.TypeRef{Kind: contractmodel.KindScalar, Name: "string"},
	})
}

func addFlattenedOneofVariantFields(
	oneof *contractmodel.Oneof,
	messageIndex map[string]*contractmodel.Message,
	expanded *[]*contractmodel.Field,
	seen map[string]struct{},
	skipFields map[string]struct{},
) {
	for _, variant := range oneof.Variants {
		skipFields[variant.FieldName] = struct{}{}
		if variant.Type == nil || variant.Type.Kind != contractmodel.KindMessage {
			continue
		}
		nested := messageIndex[variant.Type.Name]
		for _, field := range flattenChildFields("", nested, messageIndex) {
			field.Optional = true
			field.HasPresence = true
			addExpandedField(expanded, seen, field)
		}
	}
}

func flattenChildFields(
	prefix string,
	message *contractmodel.Message,
	messageIndex map[string]*contractmodel.Message,
) []*contractmodel.Field {
	if message == nil {
		return nil
	}

	var result []*contractmodel.Field
	for _, field := range message.Fields {
		if field.Annotations.Flatten && field.Type != nil && field.Type.Kind == contractmodel.KindMessage {
			result = append(
				result,
				flattenChildFields(
					prefix+field.Annotations.FlattenPrefix,
					messageIndex[field.Type.Name],
					messageIndex,
				)...,
			)
			continue
		}
		cloned := cloneField(field)
		cloned.Name = prefix + field.Name
		cloned.JSONName = prefix + field.Name
		cloned.Optional = true
		cloned.HasPresence = true
		result = append(result, cloned)
	}
	return result
}

func addExpandedField(fields *[]*contractmodel.Field, seen map[string]struct{}, field *contractmodel.Field) {
	if field == nil {
		return
	}
	if _, exists := seen[field.Name]; exists {
		return
	}
	seen[field.Name] = struct{}{}
	*fields = append(*fields, field)
}

func cloneField(field *contractmodel.Field) *contractmodel.Field {
	if field == nil {
		return nil
	}
	cloned := *field
	return &cloned
}

func hasDefault(field *contractmodel.Field) bool {
	return field.Repeated || field.IsMap || shouldSkipNone(field) || defaultLiteral(field) != ""
}

func pythonFieldType(field *contractmodel.Field) string {
	if field != nil && field.Repeated {
		baseField := *field
		baseField.Repeated = false
		return "list[" + pythonBaseType(&baseField) + "]"
	}
	base := pythonBaseType(field)
	if shouldSkipNone(field) {
		return base + " | None"
	}
	return base
}

func pythonFieldDefault(field *contractmodel.Field) string {
	if field.Repeated {
		return " = field(default_factory=list)"
	}
	if field.IsMap {
		return " = field(default_factory=dict)"
	}
	if shouldSkipNone(field) {
		return " = None"
	}
	if literal := defaultLiteral(field); literal != "" {
		return " = " + literal
	}
	if field.Type != nil && field.Type.Kind == contractmodel.KindWellKnown {
		switch field.Type.WellKnown {
		case contractmodel.WellKnownAny, contractmodel.WellKnownStruct, contractmodel.WellKnownValue:
			return " = field(default_factory=dict)"
		case contractmodel.WellKnownListValue, contractmodel.WellKnownFieldMask:
			return " = field(default_factory=list)"
		case contractmodel.WellKnownEmpty:
			return " = field(default_factory=Empty)"
		case contractmodel.WellKnownDuration, contractmodel.WellKnownTimestamp,
			contractmodel.WellKnownDoubleWrap, contractmodel.WellKnownFloatWrap,
			contractmodel.WellKnownInt64Wrap, contractmodel.WellKnownUInt64Wrap,
			contractmodel.WellKnownInt32Wrap, contractmodel.WellKnownUInt32Wrap,
			contractmodel.WellKnownBoolWrap, contractmodel.WellKnownStringWrap,
			contractmodel.WellKnownBytesWrap:
			// handled by defaultLiteral / shouldSkipNone
		}
	}
	if field.Type != nil && field.Type.Kind == contractmodel.KindMessage {
		return " = field(default_factory=" + field.Type.Name + ")"
	}
	return ""
}

func pythonBaseType(field *contractmodel.Field) string {
	if field == nil || field.Type == nil {
		return pythonAnyType
	}
	ref := field.Type
	switch ref.Kind {
	case contractmodel.KindEnum:
		return ref.Name
	case contractmodel.KindMessage:
		return ref.Name
	case contractmodel.KindMap:
		keyType := pythonBaseType(&contractmodel.Field{Type: ref.MapKey})
		valueType := pythonBaseType(&contractmodel.Field{Type: ref.MapValue})
		return fmt.Sprintf("dict[%s, %s]", keyType, valueType)
	case contractmodel.KindWellKnown:
		return pythonWellKnownType(field)
	case contractmodel.KindScalar:
		return pythonScalar(field)
	default:
		return pythonAnyType
	}
}

func pythonWellKnownType(field *contractmodel.Field) string {
	switch field.Type.WellKnown {
	case contractmodel.WellKnownAny, contractmodel.WellKnownStruct, contractmodel.WellKnownValue:
		return "dict[str, Any]"
	case contractmodel.WellKnownListValue:
		return "list[Any]"
	case contractmodel.WellKnownTimestamp:
		switch field.Annotations.TimestampFormat {
		case sebufhttp.TimestampFormat_TIMESTAMP_FORMAT_UNIX_MILLIS,
			sebufhttp.TimestampFormat_TIMESTAMP_FORMAT_UNIX_SECONDS:
			return pythonIntType
		case sebufhttp.TimestampFormat_TIMESTAMP_FORMAT_UNSPECIFIED,
			sebufhttp.TimestampFormat_TIMESTAMP_FORMAT_RFC3339,
			sebufhttp.TimestampFormat_TIMESTAMP_FORMAT_DATE:
			return pythonStringType
		default:
			return pythonStringType
		}
	case contractmodel.WellKnownDuration:
		return pythonStringType
	case contractmodel.WellKnownFieldMask:
		return "list[str]"
	case contractmodel.WellKnownEmpty:
		return emptyTypeName
	case contractmodel.WellKnownDoubleWrap, contractmodel.WellKnownFloatWrap,
		contractmodel.WellKnownInt64Wrap, contractmodel.WellKnownUInt64Wrap,
		contractmodel.WellKnownInt32Wrap, contractmodel.WellKnownUInt32Wrap,
		contractmodel.WellKnownBoolWrap, contractmodel.WellKnownStringWrap,
		contractmodel.WellKnownBytesWrap:
		return pythonScalar(field)
	default:
		return pythonAnyType
	}
}

func pythonScalar(field *contractmodel.Field) string {
	kind := field.Type.Name
	switch kind {
	case "double", "float":
		return pythonFloatType
	case "int64", "uint64", "fixed64", "sfixed64", "sint64":
		if field.Annotations.Int64Encoding != sebufhttp.Int64Encoding_INT64_ENCODING_NUMBER {
			return pythonStringType
		}
		return pythonIntType
	case "int32", "fixed32", "uint32", "sfixed32", "sint32":
		return pythonIntType
	case "bool":
		return pythonBoolType
	case pythonBytesType:
		return pythonBytesType
	case "string":
		return pythonStringType
	default:
		return pythonAnyType
	}
}

func shouldSkipNone(field *contractmodel.Field) bool {
	if field == nil || field.Type == nil {
		return true
	}
	if field.Annotations.Nullable || field.IsOneofVariant || field.Optional || field.HasPresence {
		return true
	}
	if field.Type.Kind == contractmodel.KindWellKnown {
		//nolint:exhaustive // wrappers are the only well-known cases that should imply None here
		switch field.Type.WellKnown {
		case contractmodel.WellKnownDoubleWrap, contractmodel.WellKnownFloatWrap,
			contractmodel.WellKnownInt64Wrap, contractmodel.WellKnownUInt64Wrap,
			contractmodel.WellKnownInt32Wrap, contractmodel.WellKnownUInt32Wrap,
			contractmodel.WellKnownBoolWrap, contractmodel.WellKnownStringWrap,
			contractmodel.WellKnownBytesWrap:
			return true
		}
	}
	return false
}

func usesEmptyBehavior(field *contractmodel.Field) bool {
	return field.Type != nil &&
		field.Type.Kind == contractmodel.KindMessage &&
		(field.Annotations.EmptyBehavior == sebufhttp.EmptyBehavior_EMPTY_BEHAVIOR_NULL ||
			field.Annotations.EmptyBehavior == sebufhttp.EmptyBehavior_EMPTY_BEHAVIOR_OMIT)
}

func serializeExpr(field *contractmodel.Field) string {
	if field.Repeated {
		baseField := *field
		baseField.Repeated = false
		baseField.IsMap = false
		return "[" + serializeBaseExpr(&baseField, "item") + " for item in value]"
	}
	if field.IsMap {
		keyExpr := serializeBaseExpr(&contractmodel.Field{Type: field.Type.MapKey}, "key")
		valField := &contractmodel.Field{Type: field.Type.MapValue, Annotations: field.Annotations}
		valueExprInner := serializeBaseExpr(valField, "item")
		return "{" + keyExpr + ": " + valueExprInner + " for key, item in value.items()}"
	}
	return serializeBaseExpr(field, "value")
}

func serializeBaseExpr(field *contractmodel.Field, valueExpr string) string {
	if field == nil || field.Type == nil {
		return valueExpr
	}
	ref := field.Type
	switch ref.Kind {
	case contractmodel.KindEnum:
		if field.Annotations.EnumEncoding == sebufhttp.EnumEncoding_ENUM_ENCODING_NUMBER {
			return "int(" + valueExpr + ")"
		}
		return "_" + ref.Name + "_TO_JSON[" + valueExpr + "]"
	case contractmodel.KindMessage:
		return valueExpr + ".to_dict()"
	case contractmodel.KindWellKnown:
		//nolint:exhaustive // unsupported well-known kinds fall back to pass-through JSON values
		switch ref.WellKnown {
		case contractmodel.WellKnownEmpty:
			return "{}"
		case contractmodel.WellKnownFieldMask:
			return valueExpr
		case contractmodel.WellKnownBytesWrap:
			return serializeScalarBytes(field, valueExpr)
		default:
			return valueExpr
		}
	case contractmodel.KindScalar:
		if ref.Name == "bytes" {
			return serializeScalarBytes(field, valueExpr)
		}
		return valueExpr
	case contractmodel.KindMap:
		return valueExpr
	default:
		return valueExpr
	}
}

func serializeScalarBytes(field *contractmodel.Field, valueExpr string) string {
	return `_encode_bytes(` + valueExpr + `, "` + bytesEncodingName(field.Annotations.BytesEncoding) + `")`
}

func deserializeExpr(field *contractmodel.Field, valueExpr string) string {
	if field.Repeated {
		baseField := *field
		baseField.Repeated = false
		baseField.IsMap = false
		return "[" + deserializeBaseExpr(&baseField, "item") + " for item in " + valueExpr + "]"
	}
	if field.IsMap {
		keyExpr := deserializeBaseExpr(&contractmodel.Field{Type: field.Type.MapKey}, "key")
		valField := &contractmodel.Field{Type: field.Type.MapValue, Annotations: field.Annotations}
		valueInner := deserializeBaseExpr(valField, "item")
		return "{" + keyExpr + ": " + valueInner + " for key, item in " + valueExpr + ".items()}"
	}
	return deserializeBaseExpr(field, valueExpr)
}

func deserializeBaseExpr(field *contractmodel.Field, valueExpr string) string {
	if field == nil || field.Type == nil {
		return valueExpr
	}
	ref := field.Type
	switch ref.Kind {
	case contractmodel.KindEnum:
		if field.Annotations.EnumEncoding == sebufhttp.EnumEncoding_ENUM_ENCODING_NUMBER {
			return ref.Name + "(" + valueExpr + ")"
		}
		return "_" + ref.Name + "_FROM_JSON[str(" + valueExpr + ")]"
	case contractmodel.KindMessage:
		return ref.Name + ".from_dict(" + valueExpr + ")"
	case contractmodel.KindWellKnown:
		//nolint:exhaustive // unsupported well-known kinds fall back to pass-through JSON values
		switch ref.WellKnown {
		case contractmodel.WellKnownEmpty:
			return "Empty.from_dict(" + valueExpr + ")"
		case contractmodel.WellKnownBytesWrap:
			return `_decode_bytes(str(` + valueExpr + `), "` + bytesEncodingName(field.Annotations.BytesEncoding) + `")`
		default:
			return valueExpr
		}
	case contractmodel.KindScalar:
		switch ref.Name {
		case "bytes":
			return `_decode_bytes(str(` + valueExpr + `), "` + bytesEncodingName(field.Annotations.BytesEncoding) + `")`
		case "int64", "uint64", "fixed64", "sfixed64", "sint64":
			if field.Annotations.Int64Encoding != sebufhttp.Int64Encoding_INT64_ENCODING_NUMBER {
				return "str(" + valueExpr + ")"
			}
			return "int(" + valueExpr + ")"
		case "int32", "fixed32", "uint32", "sfixed32", "sint32":
			return "int(" + valueExpr + ")"
		case "double", "float":
			return "float(" + valueExpr + ")"
		case "bool":
			return "bool(" + valueExpr + ")"
		case "string":
			return "str(" + valueExpr + ")"
		default:
			return valueExpr
		}
	case contractmodel.KindMap:
		return valueExpr
	default:
		return valueExpr
	}
}

func defaultLiteral(field *contractmodel.Field) string {
	if field == nil || field.Type == nil {
		return ""
	}
	switch field.Type.Kind {
	case contractmodel.KindEnum:
		return firstEnumValue(field.Type.Name)
	case contractmodel.KindMessage, contractmodel.KindMap:
		return ""
	case contractmodel.KindScalar:
		switch pythonScalar(field) {
		case pythonFloatType:
			return "0.0"
		case pythonIntType:
			return "0"
		case pythonBoolType:
			return "False"
		case pythonStringType:
			return `""`
		case pythonBytesType:
			return `b""`
		}
	case contractmodel.KindWellKnown:
		//nolint:exhaustive // unsupported well-known kinds either use explicit literals above or no literal
		switch field.Type.WellKnown {
		case contractmodel.WellKnownTimestamp, contractmodel.WellKnownDuration:
			if pythonWellKnownType(field) == pythonIntType {
				return "0"
			}
			return `""`
		case contractmodel.WellKnownFieldMask, contractmodel.WellKnownListValue:
			return ""
		case contractmodel.WellKnownDoubleWrap, contractmodel.WellKnownFloatWrap,
			contractmodel.WellKnownInt64Wrap, contractmodel.WellKnownUInt64Wrap,
			contractmodel.WellKnownInt32Wrap, contractmodel.WellKnownUInt32Wrap,
			contractmodel.WellKnownBoolWrap, contractmodel.WellKnownStringWrap,
			contractmodel.WellKnownBytesWrap:
			return ""
		default:
			return ""
		}
	default:
		return ""
	}
	return ""
}

func firstEnumValue(name string) string {
	return name + "(0)"
}

func bytesEncodingName(encoding sebufhttp.BytesEncoding) string {
	switch encoding {
	case sebufhttp.BytesEncoding_BYTES_ENCODING_UNSPECIFIED,
		sebufhttp.BytesEncoding_BYTES_ENCODING_BASE64:
		return "base64"
	case sebufhttp.BytesEncoding_BYTES_ENCODING_BASE64_RAW:
		return "base64_raw"
	case sebufhttp.BytesEncoding_BYTES_ENCODING_BASE64URL:
		return "base64url"
	case sebufhttp.BytesEncoding_BYTES_ENCODING_BASE64URL_RAW:
		return "base64url_raw"
	case sebufhttp.BytesEncoding_BYTES_ENCODING_HEX:
		return "hex"
	default:
		return "base64"
	}
}

func packageMessages(pkg *contractmodel.Package) []*contractmodel.Message {
	messages := make([]*contractmodel.Message, 0, len(pkg.Messages)+1)
	messages = append(messages, pkg.Messages...)
	if needsSyntheticEmpty(pkg) {
		messages = append(messages, &contractmodel.Message{Name: emptyTypeName})
	}
	sort.Slice(messages, func(i, j int) bool {
		return messages[i].Name < messages[j].Name
	})
	return messages
}

func needsSyntheticEmpty(pkg *contractmodel.Package) bool {
	for _, service := range pkg.Services {
		for _, method := range service.Methods {
			if method.InputType == emptyTypeName || method.ResponseType == emptyTypeName {
				return true
			}
		}
	}
	for _, message := range pkg.Messages {
		for _, field := range message.Fields {
			if field.Type != nil &&
				field.Type.Kind == contractmodel.KindWellKnown &&
				field.Type.WellKnown == contractmodel.WellKnownEmpty {
				return true
			}
		}
	}
	return false
}

func isRootUnwrapMessage(message *contractmodel.Message) bool {
	return message != nil && message.Unwrap != nil && message.Unwrap.IsRoot && len(message.Fields) == 1
}

func pythonMethodName(name string) string {
	if name == "" {
		return ""
	}
	var parts []string
	var current strings.Builder
	for i, r := range name {
		if i > 0 && r >= 'A' && r <= 'Z' {
			parts = append(parts, strings.ToLower(current.String()))
			current.Reset()
		}
		current.WriteRune(r)
	}
	if current.Len() > 0 {
		parts = append(parts, strings.ToLower(current.String()))
	}
	return strings.Join(parts, "_")
}

func joinQuoted(values []string) string {
	if len(values) == 0 {
		return ""
	}
	quoted := make([]string, 0, len(values))
	for _, value := range values {
		quoted = append(quoted, `"`+value+`"`)
	}
	return strings.Join(quoted, ", ")
}
