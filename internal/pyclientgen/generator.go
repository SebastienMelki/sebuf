package pyclientgen

import (
	"flag"
	"fmt"
	"path"
	"strings"

	"google.golang.org/protobuf/compiler/protogen"

	"github.com/SebastienMelki/sebuf/internal/contractmodel"
)

const (
	pythonAnyType    = "Any"
	pythonStringType = "str"
)

type Generator struct {
	plugin *protogen.Plugin
}

func New(plugin *protogen.Plugin) *Generator {
	return &Generator{plugin: plugin}
}

func NewOptions() protogen.Options {
	var flags flag.FlagSet
	_ = flags.String("package", "", "Override Python package output path")
	return protogen.Options{ParamFunc: flags.Set}
}

func (g *Generator) Generate() error {
	for _, pkg := range contractmodel.Packages(g.plugin.Files) {
		if err := g.generatePackage(pkg); err != nil {
			return err
		}
	}
	return nil
}

func (g *Generator) generatePackage(pkg *contractmodel.Package) error {
	packagePath := strings.ReplaceAll(pkg.Name, ".", "/")
	for _, initPath := range initFiles(packagePath) {
		gf := g.plugin.NewGeneratedFile(initPath, "")
		gf.P("")
	}

	gf := g.plugin.NewGeneratedFile(path.Join(packagePath, "contracts.py"), "")
	gf.P(`"""Code generated by protoc-gen-py-client. DO NOT EDIT."""`)
	gf.P("from __future__ import annotations")
	gf.P()
	gf.P("from dataclasses import dataclass, field")
	gf.P("from enum import Enum")
	gf.P("from typing import Any")
	gf.P()

	for _, enum := range pkg.Enums {
		gf.P("class ", enum.Name, "(str, Enum):")
		if len(enum.Values) == 0 {
			gf.P("    pass")
		} else {
			for _, value := range enum.Values {
				gf.P("    ", value, ` = "`, value, `"`)
			}
		}
		gf.P()
	}

	for _, message := range pkg.Messages {
		gf.P("@dataclass")
		gf.P("class ", message.Name, ":")
		if len(message.Fields) == 0 {
			gf.P("    pass")
			gf.P()
			continue
		}
		for _, field := range message.Fields {
			defaultValue := "None"
			if field.Repeated {
				defaultValue = "field(default_factory=list)"
			}
			gf.P("    ", field.Name, ": ", pythonType(field.Type, field.Repeated), " | None = ", defaultValue)
		}
		gf.P()
	}

	gf.P("services: dict[str, dict[str, dict[str, str]]] = {")
	for _, service := range pkg.Services {
		gf.P(`    "`, service.Name, `": {`)
		for _, method := range service.Methods {
			gf.P(
				`        "`,
				method.Name,
				`": {"request_type": "`,
				method.InputType,
				`", "response_type": "`,
				method.ResponseType,
				`"},`,
			)
		}
		gf.P("    },")
	}
	gf.P("}")
	return nil
}

func initFiles(packagePath string) []string {
	if packagePath == "" {
		return nil
	}
	parts := strings.Split(packagePath, "/")
	files := make([]string, 0, len(parts))
	current := make([]string, 0, len(parts))
	for _, part := range parts {
		current = append(current, part)
		files = append(files, path.Join(path.Join(current...), "__init__.py"))
	}
	return files
}

func pythonType(ref *contractmodel.TypeRef, repeated bool) string {
	if ref == nil {
		return pythonAnyType
	}
	base := pythonBaseType(ref)
	if repeated {
		return "list[" + base + "]"
	}
	return base
}

func pythonBaseType(ref *contractmodel.TypeRef) string {
	switch ref.Kind {
	case contractmodel.KindStruct:
		return "dict[str, " + pythonAnyType + "]"
	case contractmodel.KindTimestamp:
		return pythonStringType
	case contractmodel.KindMessage:
		return ref.Name
	case contractmodel.KindEnum:
		return pythonStringType
	case contractmodel.KindMap:
		return fmt.Sprintf("dict[%s, %s]", pythonType(ref.MapKey, false), pythonType(ref.MapValue, false))
	case contractmodel.KindScalar:
		return pythonScalar(ref.Name)
	default:
		return pythonAnyType
	}
}

func pythonScalar(kind string) string {
	switch kind {
	case "double", "float":
		return "float"
	case "int64", "uint64", "int32", "fixed64", "fixed32", "uint32", "sfixed32", "sfixed64", "sint32", "sint64":
		return "int"
	case "bool":
		return "bool"
	case "string", "bytes":
		return pythonStringType
	default:
		return pythonAnyType
	}
}
